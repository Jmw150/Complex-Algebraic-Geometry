(* ================================================================== *)
(** * 4. Proposition 1.3 reformulated                                  *)
(* ================================================================== *)

(** [conjugacy_separable_via_specialization]: a structurally explicit
    re-statement of Proposition 1.3 in terms of the specialization
    lemma. Given a separating representation, we get a finite-quotient
    distinguishing hom — exactly the conjugacy-separable property.

    The actual proof Proposition 1.3 (ungated) requires:
    1. Definition of the trace ring of a representation.
    2. The specialization lemma (axiomatized above).
    3. The fact that ρ(γ) and ρ(η) being non-conjugate in a finite
       group descends to non-conjugate finite-quotient images of γ, η.

    Step 3 needs care: ρ(γ) ≁ ρ(η) in SL(n, F_q) does NOT immediately
    give γ ≁ η in some finite quotient of Γ. The standard argument is
    to use the kernel of ρ̄ : Γ → SL(n, F_q) as the finite-index normal
    subgroup; the quotient Γ/ker(ρ̄) is a subgroup of SL(n, F_q), hence
    finite. *)

(** Statement of Prop 1.3 with specialization made explicit. *)
Definition prop_1_3_with_specialization {G : Type} (sg : StrictGroup G) :
    Prop :=
  forall (gamma eta : G),
    ~ are_conjugate sg gamma eta ->
    (* Hypothesis: there's a "nice" trace ring and trace-distinguishing
       element coming from some representation. *)
    forall (R : FGRing)
           (trace_diff : fgr_carrier R)
           (Hnz : trace_diff <> rzero (fgr_carrier R) (fgr_ring R)),
      exists (Q : Type) (sq : StrictGroup Q) (Q_list : list Q)
             (Q_complete : forall x : Q, In x Q_list)
             (phi : GroupHom sg sq),
        ~ are_conjugate sq (hom_fn phi gamma) (hom_fn phi eta).

