(** * 3. From representation to specialization                          *)
(* ================================================================== *)

(** A representation ρ : Γ → SL(n, R) lifts to a finite quotient
    when R is a f.g. integral domain. The hypothesis is that the
    trace difference of two non-conjugate elements is a nonzero
    element of R; specialize to a finite field. *)

Section SpecializationOfTraceDiff.
  Context {G : Type} (sg : StrictGroup G) (R : FGRing).

  (** Hypothesis: the trace difference tr(ρ(γ)) - tr(ρ(η)) is a
      non-zero element of R for some ρ. We package this as an
      explicit input. *)
