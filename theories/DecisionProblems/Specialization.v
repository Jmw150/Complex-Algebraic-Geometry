(* ================================================================== *)
(** * 1. Finitely generated rings (axiomatic stub)                      *)
(* ================================================================== *)

(** A finitely generated commutative ring R: every element is a
    polynomial expression in the generators. We axiomatize this
    structure since the project's Rings layer doesn't yet have
    generators-and-relations infrastructure. *)

Record FGRing : Type := mkFGR
{
  fgr_carrier : Type;
  fgr_ring    : Ring fgr_carrier;
  fgr_gens    : list fgr_carrier;
  (** Every r ∈ R is in the ring generated by [fgr_gens] — this is the
      "finitely generated" property. Stated abstractly via a
      generators predicate. *)
  fgr_in_gen_ring : forall r : fgr_carrier, True;
}.

(* ================================================================== *)
(** * 2. The specialization lemma                                       *)
(* ================================================================== *)

(** Bass-Lubotzky specialization (axiomatized).

    Statement: for any f.g. commutative ring R and any nonzero r ∈ R,
    there exists a finite ring Q (i.e., one with a list-enumeration of
    its elements) and a ring hom φ : R → Q with φ(r) ≠ 0_Q.

    This is a cornerstone of effective algebraic-geometric arguments
    over Z, used pervasively in arithmetic / linear group theory.

    The proof uses Hilbert's Nullstellensatz style argument: f.g. rings
    have a maximal ideal m with R/m a finite field, and one can choose
    m to avoid r. *)

(* CAG zero-dependent Conjecture bass_lubotzky_specialization theories/DecisionProblems/Specialization.v:70 BEGIN
Conjecture bass_lubotzky_specialization :
  forall (R : FGRing) (r : fgr_carrier R),
    r <> rzero (fgr_carrier R) (fgr_ring R) ->
    exists (Q : Type) (rq : Ring Q)
           (Q_list : list Q)
           (Q_complete : forall x : Q, In x Q_list)
           (phi : RingHom (fgr_ring R) rq),
      rhom_fn phi r <> rzero Q rq.
   CAG zero-dependent Conjecture bass_lubotzky_specialization theories/DecisionProblems/Specialization.v:70 END *)

