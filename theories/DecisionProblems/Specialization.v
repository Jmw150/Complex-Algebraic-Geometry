(** * DecisionProblems/Specialization.v

    Specialization lemma framework (Bass / Lubotzky).

    Addresses [G5] from OpenProblems.v.

    The classical specialization lemma states:
        If R is a finitely generated integral domain and 0 ≠ r ∈ R,
        then there exists a homomorphism φ : R → F to a finite field
        F such that φ(r) ≠ 0.

    This is the key step in the proof of Proposition 1.3 ((D) ⇒
    conjugacy separable):
    1. By (D), tr(ρ(γ)) ≠ tr(ρ(η)) for some ρ : Γ → SL(n, R).
    2. The trace difference is a nonzero element of a f.g. integral
       domain (the trace ring of ρ).
    3. Specialize to a finite field; ρ specializes to ρ̄ : Γ → SL(n, F_q).
    4. The image group is finite, so distinguishes the conjugacy
       classes by trace in a finite quotient.

    This file:
    - Records the abstract specialization lemma as a named axiom.
    - Builds the bridge from a separating representation over an
      integral domain to a separating finite-quotient hom.
    - Derives Proposition 1.3 from these pieces. *)

From CAG Require Import Group.
