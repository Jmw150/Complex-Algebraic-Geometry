(* ================================================================== *)
(** * 5. What's still open after Procesi                               *)
(* ================================================================== *)

(** Even with Procesi, the question "no SL(n)-trace pair for n ≥ 3"
    requires MORE: Procesi separates GIT orbits but the gap from GIT
    orbit to conjugation orbit involves the "trace-only" boundary
    (where reps degenerate to non-irreducible).

    Concretely, at SL(2,F): Procesi-Horowitz gives that trace-equivalence
    coincides with conjugacy modulo INVERSION (γ ∼ η or γ ∼ η⁻¹). The
    "modulo inversion" is the trace-boundary phenomenon.

    For n ≥ 3, the analogous "mod boundary" is more elaborate. A clean
    statement for n=3 would be:

      "If γ, η ∈ F_r are SL(3,ℂ)-trace-equivalent, then they are
       conjugate in F_r modulo a finite group of automorphisms of F_r
       (perhaps including inversion and other symmetries)."

    No such clean statement is currently known. *)
