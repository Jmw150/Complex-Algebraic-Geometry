(** * DecisionProblems/Scaffolding/Procesi.v

    SCAFFOLDING for Procesi's theorem on polynomial invariants of
    n-tuples of n×n matrices under simultaneous conjugation, restricted
    to what's needed to attack [open_SLn_trace_pair n] for n ≥ 3.

    Procesi (1976): The ring of polynomial invariants of m-tuples of
    n×n matrices under simultaneous conjugation is generated by traces
    of all monomials of length ≤ n² in the m matrices.

    For our application (open_SLn_trace_pair): if two F_r elements have
    equal traces under EVERY SL(n,ℂ) representation (equivalently, equal
    trace polynomials in r matrix variables under SL(n) conjugation),
    Procesi gives that they're identified in the GIT quotient
    SL(n,ℂ)^r / SL(n,ℂ). Combined with Procesi's separation refinement,
    this would imply they're either conjugate or differ by a "boundary"
    in the GIT quotient.

    We do NOT prove Procesi here — it's a major theorem in invariant
    theory. We scaffold the statement and the chain of reductions. *)

