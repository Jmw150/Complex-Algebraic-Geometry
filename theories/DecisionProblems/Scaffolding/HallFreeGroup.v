(** **HALL'S FREE FACTOR THEOREM:** Given γ ∈ F_r with γ ≠ e, there
    exists a finite-index subgroup Δ ≤ F_r such that γ is part of a
    free generating set for Δ (so ⟨γ⟩ is a free factor of Δ).

    The proof uses Stallings folding / Schreier coset graphs:
    1. Build the coset graph of ⟨γ⟩ in F_r (infinite).
    2. Fold it into a finite "core" graph using Stallings folding.
    3. The core graph has finitely many vertices and edges.
    4. Add edges to create a finite-index subgroup containing ⟨γ⟩
       as a free factor. *)

