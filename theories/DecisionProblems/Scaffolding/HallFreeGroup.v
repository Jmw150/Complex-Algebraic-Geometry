(** * 1. Subgroup structure for free groups                            *)
(* ================================================================== *)

(** A finitely generated subgroup of [RWord r], represented as a list
    of generators. *)

Definition fg_subgroup (r : nat) := list (RWord r).

(** Membership in the subgroup generated by a list of elements. *)
Inductive subgroup_gen {r : nat} (gens : fg_subgroup r) : RWord r -> Prop :=
  | sg_e : subgroup_gen gens (@rword_e r)
  | sg_gen : forall g, In g gens -> subgroup_gen gens g
  | sg_inv : forall g, subgroup_gen gens g ->
                       subgroup_gen gens (rword_inv g)
  | sg_mul : forall g h, subgroup_gen gens g -> subgroup_gen gens h ->
                         subgroup_gen gens (rword_mul g h).

