(** **CHAIN to theorem_1_6_free_groups:**

    1. Given γ ∈ F_r with γ ≠ e, [hall_finite_index_free_factor] gives
       Δ ⊴ F_r with [F_r : Δ] = k and ⟨γ⟩ a free factor of Δ.

    2. For any matrix group MG, take ρ : ⟨γ⟩ → MG sending γ to a
       chosen matrix A with tr(A) chosen so that tr(A^j) ≠ tr(A^l)
       for j ≠ l up to some bound. Such A always exists (e.g.,
       hyperbolic).

    3. Extend ρ to ρ_Δ : Δ → MG via the free-factor structure (trivially
       on the other factors).

    4. Induce ρ_Δ to ρ_F_r : F_r → SL(k·n, F) via Frobenius induction.

    5. The trace formula gives:
         tr(ρ_F_r(g)) = Σ_{i: σ_g(i)=i} tr(ρ_Δ(h_i))
       where σ_g is the coset permutation, h_i is the cocycle.

    6. For γ itself: σ_γ has fixed points exactly at cosets where γ
       acts trivially on the index. The trace becomes a polynomial in
       tr(A^j) values.

    7. For η non-conjugate to γ: σ_η has DIFFERENT fixed-point structure,
       giving a different trace polynomial. *)

