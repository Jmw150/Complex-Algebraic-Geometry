(** * DecisionProblems/Scaffolding/HallFreeGroup.v

    SCAFFOLDING for Marshall Hall's theorem on subgroups of finitely
    generated free groups, restricted to what's needed for the
    Hall-Frobenius construction in the McReynolds property-(B) chain.

    Hall (1949): every finitely generated subgroup H of a free group
    F is itself free, and every finitely generated subgroup is a free
    factor of a finite-index subgroup of F.

    For our application (theorem_1_6_free_groups): given γ ∈ F_r,
    Hall's theorem provides a finite-index normal subgroup Δ ⊴ F_r
    such that ⟨γ⟩ is a free factor of Δ. From this Δ, the induced
    representation construction + Frobenius trace formula gives a
    representation distinguishing γ from any non-conjugate η. *)

