(* ================================================================== *)
(** * 1. Control-affine systems                                         *)
(* ================================================================== *)

(** A control-affine system: a drift vector field plus a finite list of
    control vector fields. *)
Record ControlSystem (M : SmoothManifold) : Type := mkCS {
  cs_drift : VectorField M;
  cs_controls : list (VectorField M);
}.

Arguments cs_drift {M} _.
Arguments cs_controls {M} _.

(** A system is *driftless* if its drift vanishes identically. *)
Definition is_driftless {M : SmoothManifold} (S : ControlSystem M) : Prop :=
  cs_drift S = zero_vf M.

(** Number of control inputs. *)
Definition cs_num_controls {M : SmoothManifold} (S : ControlSystem M) : nat :=
  length (cs_controls S).

(* ================================================================== *)
(** * 2. The Lie algebra generated by a control system                  *)
(* ================================================================== *)

(** The Lie algebra rank condition (LARC) at a point p ∈ M is the
    statement that the Lie algebra generated (under bracketing) by the
    drift and control vector fields spans T_p M. We formalize this as
    the existence of a finite list of iterated brackets whose values at
    p span [TangentSpace M p].

    Axiomatized here as a predicate; concrete combinations have to be
    verified case-by-case in any actual application. *)
