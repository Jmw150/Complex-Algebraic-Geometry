(** * 5. Open problems                                                  *)
(* ================================================================== *)

(** Following Coron's "Control and nonlinearity" survey, several
    well-known open problems in geometric control: *)

(** ** Open question (small-time local controllability for affine systems).

    Sussmann's sufficient condition for STLC is far from necessary. Coron
    has asked: is there a clean *necessary and sufficient* condition for
    STLC of a control-affine system at an equilibrium of the drift?
    Status: open in general; resolved only for restricted classes. *)
(* CAG zero-dependent Definition open_question_STLC_characterization theories/Control/Geometric.v:147 BEGIN
Definition open_question_STLC_characterization : Prop :=
  exists (P : forall {M : SmoothManifold} (S : ControlSystem M)
              (U : list R -> Prop) (p : sm_carrier M), Prop),
    forall {M : SmoothManifold} (S : ControlSystem M)
           (U : list R -> Prop) (p : sm_carrier M),
      vf_fn (cs_drift S) p = ts_zero ->
      P S U p <-> is_STLC S U p.
   CAG zero-dependent Definition open_question_STLC_characterization theories/Control/Geometric.v:147 END *)

(** ** Open question (sub-Riemannian geodesics: smoothness).

    For sub-Riemannian metrics, are length-minimizing geodesics always
    C^∞? In Riemannian geometry yes; in sub-Riemannian the question is
    open in general because of "abnormal" minimizers. The Sard-type
    conjecture for end-point maps is the canonical formulation. *)
Definition open_question_sR_geodesic_smoothness : Prop :=
  forall {M : SmoothManifold} (S : ControlSystem M),
    is_driftless S ->
    (* every length-minimizer is C^∞; placeholder predicate for now *)
    True.

