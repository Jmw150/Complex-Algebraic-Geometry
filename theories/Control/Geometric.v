(** * Control/Geometric.v
    Geometric control theory on smooth manifolds.

    The basic objects: a control-affine system on a smooth manifold M is
        ẋ = f₀(x) + Σᵢ uᵢ fᵢ(x),    u ∈ U ⊆ ℝᵐ
    with f₀ a "drift" vector field and f₁,…,fₘ "control" vector fields.

    Key questions formalized here:
      - Which states are reachable from a given starting point?
      - When does the Lie algebra rank condition (LARC) hold?
      - When is the system controllable (Chow–Rashevskii)?
      - What is the structure of the optimal-control problem (Pontryagin)?

    References:
      Agrachev–Sachkov, *Control Theory from the Geometric Viewpoint*,
        chapters 2 (vector fields, brackets), 3 (orbits), 4–5 (controllability),
        12–13 (optimal control / Pontryagin).
      Bullo–Lewis, *Geometric Control of Mechanical Systems*, chapters 6–7.
      Sontag, *Mathematical Control Theory* (reference).
*)

