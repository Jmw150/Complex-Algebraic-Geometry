(** Vanishing/LefschetzHyperplane.v — Lefschetz hyperplane theorem

    Theorem (Lefschetz hyperplane theorem):
      Let M be a compact projective manifold of complex dimension n,
      and let V = M ∩ H be a smooth hyperplane section.  Then the
      restriction map on homology
          H_q(V, Z) -> H_q(M, Z)
      is an isomorphism for q < n-1 and surjective for q = n-1.

      Equivalently on cohomology:
          H^q(M, Z) -> H^q(V, Z)
      is an isomorphism for q < n-1 and injective for q = n-1.

    Standard proof: Morse theory on the complement M \ V shows
    M has the homotopy type of V with cells of dimension ≥ n attached.
    The analytic proof via the Lefschetz pencil is also classical.

    Corollaries for smooth projective hypersurfaces V ⊂ P^n:
    - H^{2k-1}(V) = 0 for k ≠ n/2  (odd cohomology except middle)
    - H^{2k}(V) generated by k-plane section class for k < n/2
    - If dim V ≥ 2 then π_1(V) = 0 (simply connected)

    References: ag.org §Lefschetz theorem on hyperplane sections *)

