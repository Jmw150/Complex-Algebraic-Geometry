(** * AxTheory/GluingSetup.v
    The specific gluing setup used in the proof of the conservativity theorem
    (Discussion 4.10.5 / Theorem 4.10.7).

    Setup:
    - E  = Cl(Th)      (algebraic classifying category, FP)
    - E' = AxCl(Th)    (Ax classifying CCC)
    - I  : E → E'      (the canonical FP-functor from RelativeFreeCCC.v)
    - TC = E'          (the relatively free CCC generated by E)
    - T  : E' → [E^op, Set]  defined as T := よ_{E'} composed with precomposition by I
    - Gl(T) is the glued category
    - J  : E → Gl(T)   is the functor defined by the Yoneda elements

    Key facts:
    - Gl(T) is a CCC (Lemma 4.10.3)
    - P2 : Gl(T) → E' is a CCC functor
    - J is full, faithful, and preserves finite products
    - P2 ∘ J = I (on objects)
    - By relative freeness, there is a CCC functor Ī : E' → Gl(T)
      with P2 ∘ Ī ≅ id_{E'}
    - Fullness and faithfulness of I follows *)

