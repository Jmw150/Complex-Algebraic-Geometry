    + exact IH.
Qed.

(** Translation commutes with substitution at the term level (modulo AxThEq). *)
Lemma tr_term_subst (Sg : Signature) (αs βs : list Sg.(sg_ty))
    (f_ts : list (Term Sg)) (Hft : Forall2 (HasType Sg αs) f_ts βs)
    (t : Term Sg) (τ : Sg.(sg_ty))
    (Ht : HasType Sg βs t τ) :
    AxThEq Sg [] [I_obj αs]
      (tr_term αs (subst_term Sg f_ts t))
      (ax_subst [tr_terms αs f_ts] (tr_term βs t))
      (ax_ground τ).
Proof.
  unfold tr_term.
  rewrite (atom_subst Sg αs f_ts t βs τ Hft Ht).
  (* LHS = ax_subst (unpack αs) (ax_subst (map atom f_ts) (atom t))
     RHS = ax_subst [tr_terms αs f_ts] (ax_subst (unpack βs) (atom t)) *)
  (* Apply ax_subst_comp on both sides to reduce to ax_subst sub_LHS / sub_RHS (atom t). *)
  assert (Hatom_ft : Forall2 (AxHasType Sg (List.map ax_ground αs))
                              (List.map atom f_ts)
                              (List.map ax_ground βs)).
  { clear t τ Ht.
    induction Hft as [|t' σ ts' βs' Ht' Hts' IHts].
    - constructor.
    - simpl. constructor.
      + apply atom_preserves_type. exact Ht'.
      + exact IHts. }
  assert (Hatom_t : AxHasType Sg (List.map ax_ground βs) (atom t) (ax_ground τ)).
  { apply atom_preserves_type. exact Ht. }
  assert (Hlen_atom_ft : length (List.map atom f_ts) = length (List.map ax_ground βs)).
  { rewrite !List.length_map. exact (Forall2_length Hft). }
  rewrite (ax_subst_comp Sg (List.map ax_ground βs)
             (unpack αs) (List.map atom f_ts) (atom t) (ax_ground τ)
             Hlen_atom_ft Hatom_t).
  (* Now LHS becomes: ax_subst (map (ax_subst (unpack αs)) (map atom f_ts)) (atom t)
     RHS:             ax_subst [tr_terms αs f_ts] (ax_subst (unpack βs) (atom t)) *)
  (* Apply ax_subst_comp on RHS with M = atom t in context (map ax_ground βs). *)
  assert (Hunpack_β : Forall2 (AxHasType Sg [I_obj βs])
                              (unpack βs) (List.map ax_ground βs)).
  { apply unpack_typed. }
  assert (Hlen_unpack_β : length (unpack βs) = length (List.map ax_ground βs)).
  { exact (Forall2_length Hunpack_β). }
  rewrite (ax_subst_comp Sg (List.map ax_ground βs)
             [tr_terms αs f_ts] (unpack βs) (atom t) (ax_ground τ)
             Hlen_unpack_β Hatom_t).
  (* Now both sides are ax_subst sub_X (atom t). Apply ax_subst_cong with Hatom_t. *)
  apply (ax_subst_cong Sg [] [I_obj αs] (List.map ax_ground βs)
           (List.map (ax_subst (unpack αs)) (List.map atom f_ts))
           (List.map (ax_subst [tr_terms αs f_ts]) (unpack βs))
           (atom t) (atom t) (ax_ground τ)).
  - (* Forall2 typing of LHS sub *)
    rewrite List.map_map.
    (* Goal: Forall2 (AxHasType Sg [I_obj αs])
              (map (fun x => ax_subst (unpack αs) (atom x)) f_ts)
              (map ax_ground βs).
       Build pointwise from Hft. *)
    clear Hatom_t Hatom_ft Hlen_atom_ft Hunpack_β Hlen_unpack_β Ht t τ.
    induction Hft as [|t' σ ts' βs' Ht' Hts' IH'].
    + simpl. constructor.
    + simpl. constructor.
      * apply (ax_subst_preserves_type Sg [I_obj αs] (List.map ax_ground αs)
                 (unpack αs) (unpack_typed Sg αs)).
        apply atom_preserves_type. exact Ht'.
      * exact IH'.
  - (* Forall2 typing of RHS sub *)
    assert (HtrFs : AxHasType Sg [I_obj αs] (tr_terms αs f_ts) (I_obj βs)).
    { apply tr_terms_typed. exact Hft. }
    clear Hatom_t Hatom_ft Hlen_atom_ft Hlen_unpack_β Ht t τ.
    (* Goal: Forall2 (AxHasType Sg [I_obj αs])
              (map (ax_subst [tr_terms αs f_ts]) (unpack βs))
              (map ax_ground βs). *)
    assert (Goal2 : forall xs ys,
              Forall2 (AxHasType Sg [I_obj βs]) xs ys ->
              Forall2 (AxHasType Sg [I_obj αs])
                      (List.map (ax_subst [tr_terms αs f_ts]) xs) ys).
    { intros xs ys H.
      induction H as [|x β xs' ys' Hx Hrest IHrest].
      - simpl. constructor.
      - simpl. constructor.
        + apply (ax_subst_preserves_type Sg [I_obj αs] [I_obj βs]
                   [tr_terms αs f_ts]).
          * constructor; [exact HtrFs|constructor].
          * exact Hx.
        + exact IHrest. }
    apply Goal2. apply unpack_typed.
  - (* Pointwise AxThEq for the two substitutions *)
    intros i βk s s' Hβk Hsi Hsi'.
    (* βk = ax_ground (βs[i]) *)
    rewrite List.nth_error_map in Hβk.
    destruct (List.nth_error βs i) as [β_i|] eqn:Eβi; [|discriminate].
    injection Hβk as <-.
    (* s = (map (ax_subst (unpack αs)) (map atom f_ts))[i]
         = ax_subst (unpack αs) (atom (f_ts[i])) *)
    rewrite List.map_map in Hsi.
    rewrite List.nth_error_map in Hsi.
    destruct (List.nth_error f_ts i) as [t_i|] eqn:Eti; [|discriminate].
    injection Hsi as <-.
    (* s' = (map (ax_subst [tr_terms αs f_ts]) (unpack βs))[i]
          = ax_subst [tr_terms αs f_ts] (proj_at (map ax_ground βs) i (ax_var 0)) *)
    unfold unpack in Hsi'.
    rewrite List.map_map in Hsi'.
    rewrite List.nth_error_map in Hsi'.
    rewrite (nth_error_seq_lt 0 (length βs) i) in Hsi'.
    + cbn in Hsi'. injection Hsi' as <-.
      rewrite ax_subst_proj_at. cbn [ax_subst].
      (* Goal: AxThEq [] [I_obj αs]
                 (ax_subst (unpack αs) (atom t_i))
                 (proj_at (map ax_ground βs) i (tr_terms αs f_ts))
                 (ax_ground β_i) *)
      apply axeq_sym.
      (* Use proj_at_pack. *)
      set (Ms := List.map (tr_term αs) f_ts).
      change (tr_terms αs f_ts) with (pack Ms).
      assert (HMs : Forall2 (AxHasType Sg [I_obj αs]) Ms (List.map ax_ground βs)).
      { apply map_tr_term_typed. exact Hft. }
      assert (HMsi : List.nth_error Ms i =
                     Some (tr_term αs t_i)).
      { unfold Ms. rewrite List.nth_error_map, Eti. reflexivity. }
      apply (proj_at_pack Sg [I_obj αs] (List.map ax_ground βs) Ms i
               (tr_term αs t_i) (ax_ground β_i) HMs HMsi).
      rewrite List.nth_error_map, Eβi. reflexivity.
    + apply List.nth_error_Some. rewrite Eβi. discriminate.
  - apply axeq_refl. exact Hatom_t.
Qed.

(** Translation commutes with substitution at the term-list level. *)
Lemma tr_terms_subst (Sg : Signature) (αs βs γs : list Sg.(sg_ty))
    (f_ts g_ts : list (Term Sg))
    (Hft : Forall2 (HasType Sg αs) f_ts βs)
    (Hgt : Forall2 (HasType Sg βs) g_ts γs) :
    AxThEq Sg [] [I_obj αs]
      (tr_terms αs (subst_list Sg f_ts g_ts))
      (ax_subst [tr_terms αs f_ts] (tr_terms βs g_ts))
      (I_obj γs).
Proof.
  unfold tr_terms, subst_list.
  rewrite List.map_map.
  (* Goal: AxThEq Sg [] [I_obj αs]
            (pack (map (tr_term αs ∘ subst_term Sg f_ts) g_ts))
            (ax_subst [tr_terms αs f_ts] (pack (map (tr_term βs) g_ts)))
            (I_obj γs) *)
  unfold I_obj.
  generalize dependent γs.
  induction g_ts as [|g g_ts' IHg]; intros γs Hgt.
  - inversion Hgt. subst. simpl.
    apply axeq_refl. apply ax_ht_tt.
  - destruct γs as [|γ γs']; [inversion Hgt|].
    inversion Hgt as [|g0 γ0 g_ts0 γs0 Hg Hgt' Eg Eγ]; subst.
    destruct g_ts' as [|g' g_ts''].
    + (* g_ts = [g] (singleton) *)
      inversion Hgt'. subst.
      cbn [List.map pack].
      change (List.map ax_ground [γ]) with [ax_ground γ].
      change (ax_iter_prod [ax_ground γ]) with (ax_ground γ).
      apply tr_term_subst with (βs := βs); assumption.
    + (* g_ts = g :: g' :: g_ts'' (≥2) *)
      inversion Hgt' as [|g0' γ0' g_ts0' γs0' Hg' Hgt'' Eg' Eγ']; subst.
      cbn [List.map].
      change (pack (?M :: ?M' :: ?Ms))
        with (ax_pair M (pack (M' :: Ms))).
      change (List.map ax_ground (γ :: γ0' :: γs0'))
        with (ax_ground γ :: List.map ax_ground (γ0' :: γs0')).
      change (ax_iter_prod (ax_ground γ :: List.map ax_ground (γ0' :: γs0')))
        with (ax_prod (ax_ground γ) (ax_iter_prod (List.map ax_ground (γ0' :: γs0')))).
      cbn [ax_subst].
      apply axeq_pair.
      * apply tr_term_subst with (βs := βs); assumption.
      * specialize (IHg (γ0' :: γs0') Hgt').
        cbn [List.map] in IHg.
        change (pack (?M' :: ?Ms))
          with (match Ms with [] => M' | _ => ax_pair M' (pack Ms) end).
        (* simplification *)
        change (List.map ax_ground (γ0' :: γs0'))
          with (ax_ground γ0' :: List.map ax_ground γs0') in IHg.
        change (ax_iter_prod (ax_ground γ0' :: List.map ax_ground γs0'))
          with (ax_iter_prod (List.map ax_ground (γ0' :: γs0'))) in IHg.
        exact IHg.
Qed.

