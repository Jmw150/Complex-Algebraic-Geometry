Qed.

(** The atom embedding commutes with substitution.  Stated for typed terms
    so the out-of-range branches don't bite. *)
Lemma atom_subst (Sg : Signature) (Γ : list Sg.(sg_ty))
    (sub : list (Term Sg)) :
    forall (t : Term Sg) (Γ' : list Sg.(sg_ty)) (τ : Sg.(sg_ty)),
      Forall2 (HasType Sg Γ) sub Γ' ->
      HasType Sg Γ' t τ ->
      atom (subst_term Sg sub t) =
      ax_subst (List.map atom sub) (atom t).
Proof.
  intro t.
  induction t as [n | fn args IHargs] using (Term_ind_full Sg);
    intros Γ' τ Hsub Ht.
  - inversion Ht as [n0 τ0 Hn0 | ]; subst.
    cbn [atom subst_term ax_subst].
    rewrite List.nth_error_map.
    destruct (Forall2_nth_error_l _ _ _ Hsub n τ Hn0) as [t' [Ht' _]].
    rewrite Ht'. simpl. reflexivity.
  - inversion Ht as [|fn0 args0 Hargs0 Efn0]; subst.
    simpl. f_equal.
    rewrite List.map_map.
    clear Ht.
    generalize dependent (Sg.(sg_dom) fn).
    induction IHargs as [|a args' Ha IHa IHrest]; intros dom Hargs.
    + inversion Hargs. reflexivity.
    + inversion Hargs as [|a' σ args0' dom' Ha' Hrest Ea Edom]; subst.
      simpl. f_equal.
      * exact (Ha _ σ Hsub Ha').
      * exact (IHrest _ Hrest).
Qed.

(** Substitution distributes over [proj_at]. *)
Lemma ax_subst_proj_at (Sg : Signature) (sub : list (AxTerm Sg)) :
    forall (αs : list (AxType Sg)) (i : nat) (M : AxTerm Sg),
      ax_subst sub (proj_at αs i M) = proj_at αs i (ax_subst sub M).
Proof.
  intro αs.
  induction αs as [|α αs' IH]; intros i M.
  - reflexivity.
  - destruct αs' as [|α' αs''].
    + reflexivity.
    + change (proj_at (α :: α' :: αs'') i ?N)
        with (match i with
              | 0   => ax_fst N
              | S j => proj_at (α' :: αs'') j (ax_snd N)
              end).
      destruct i as [|j].
      * reflexivity.
      * rewrite IH. reflexivity.
Qed.

(** β-pair on the [pack] structure. *)
Lemma proj_at_pack (Sg : Signature) (Γ : list (AxType Sg)) :
    forall (αs : list (AxType Sg)) (Ms : list (AxTerm Sg))
           (i : nat) (M : AxTerm Sg) (τ : AxType Sg),
      Forall2 (AxHasType Sg Γ) Ms αs ->
      List.nth_error Ms i = Some M ->
      List.nth_error αs i = Some τ ->
      AxThEq Sg [] Γ (proj_at αs i (pack Ms)) M τ.
Proof.
  intro αs.
  induction αs as [|α αs' IH]; intros Ms i M τ Hf HMi Hαi.
  - destruct i; discriminate.
  - inversion Hf as [|M0 α0 Ms0 αs0 HM0 Hf' EM Eα]; subst.
    destruct αs' as [|α' αs''].
    + (* αs = [α], singleton *)
      inversion Hf'. subst.
      destruct i as [|i].
      * simpl in HMi, Hαi. injection HMi as <-. injection Hαi as <-.
        simpl. apply axeq_refl. exact HM0.
      * destruct i; discriminate.
    + (* αs = α :: α' :: αs'' *)
      inversion Hf' as [|M0' α0' Ms0' αs0' HM' Hf'' EM' Eα']; subst.
      change (pack (M0 :: M0' :: Ms0'))
        with (ax_pair M0 (pack (M0' :: Ms0'))).
      destruct i as [|j].
      * simpl in HMi, Hαi. injection HMi as <-. injection Hαi as <-.
        change (proj_at (α :: α' :: αs'') 0 ?N) with (ax_fst N).
        apply axeq_beta_fst with (β := ax_iter_prod (α' :: αs'')).
        -- exact HM0.
        -- apply pack_typed. constructor; assumption.
      * change (proj_at (α :: α' :: αs'') (S j) ?N)
          with (proj_at (α' :: αs'') j (ax_snd N)).
        simpl in HMi, Hαi.
        (* Step 1: ax_snd (ax_pair M0 P) ~~ P, then proj_at congruence. *)
        assert (Hsnd : AxThEq Sg [] Γ
                  (ax_snd (ax_pair M0 (pack (M0' :: Ms0'))))
                  (pack (M0' :: Ms0'))
                  (ax_iter_prod (α' :: αs''))).
        { apply axeq_beta_snd with (α := α).
          - exact HM0.
          - apply pack_typed. constructor; assumption. }
        (* Step 2: proj_at on either side via ax_subst_cong on a singleton sub. *)
        assert (Hcong : AxThEq Sg [] Γ
                  (proj_at (α' :: αs'') j (ax_snd (ax_pair M0 (pack (M0' :: Ms0')))))
                  (proj_at (α' :: αs'') j (pack (M0' :: Ms0')))
                  τ).
        { (* congruence: rewrite both as ax_subst of the var-form *)
          set (P1 := ax_snd (ax_pair M0 (pack (M0' :: Ms0')))).
          set (P2 := pack (M0' :: Ms0')).
          assert (HP : AxThEq Sg [] Γ P1 P2 (ax_iter_prod (α' :: αs''))).
          { exact Hsnd. }
          (* Express both projections as ax_subst [P_k] (proj_at (α'::αs'') j (ax_var 0)). *)
          assert (E1 : proj_at (α' :: αs'') j P1 =
                       ax_subst [P1] (proj_at (α' :: αs'') j (ax_var 0))).
          { rewrite ax_subst_proj_at. cbn [ax_subst]. reflexivity. }
          assert (E2 : proj_at (α' :: αs'') j P2 =
                       ax_subst [P2] (proj_at (α' :: αs'') j (ax_var 0))).
          { rewrite ax_subst_proj_at. cbn [ax_subst]. reflexivity. }
          rewrite E1, E2.
          apply (ax_subst_cong Sg [] Γ
                   [ax_iter_prod (α' :: αs'')] [P1] [P2]
                   (proj_at (α' :: αs'') j (ax_var 0))
                   (proj_at (α' :: αs'') j (ax_var 0)) τ).
          - constructor.
            + apply (ax_ht_snd Sg Γ (ax_pair M0 (pack (M0' :: Ms0'))) α
                       (ax_iter_prod (α' :: αs''))).
              apply ax_ht_pair.
              * exact HM0.
              * apply pack_typed. constructor; assumption.
            + constructor.
          - constructor.
            + apply pack_typed. constructor; assumption.
            + constructor.
          - intros k βk s s' Hβk Hsk Hsk'.
            destruct k as [|k].
            + simpl in Hβk. injection Hβk as <-.
              simpl in Hsk. injection Hsk as <-.
              simpl in Hsk'. injection Hsk' as <-.
              exact HP.
            + destruct k; simpl in Hβk; discriminate.
          - apply axeq_refl.
            apply (proj_at_typed Sg [ax_iter_prod (α' :: αs'')]
                     (α' :: αs'') j τ (ax_var 0) Hαi).
            apply ax_ht_var. reflexivity. }
        (* Now combine: LHS ~~ proj_at j P2 ~~ M *)
        apply (axeq_trans Sg [] Γ
                 (proj_at (α' :: αs'') j (ax_snd (ax_pair M0 (pack (M0' :: Ms0')))))
                 (proj_at (α' :: αs'') j (pack (M0' :: Ms0')))
                 M τ).
        -- exact Hcong.
        -- apply (IH (M0' :: Ms0') j M τ).
           ++ constructor; assumption.
           ++ exact HMi.
           ++ exact Hαi.
Qed.

(** Translation of a list of typed terms is well-typed at the
    iterated-product level, given as a Forall2 (used in tr_term_subst). *)
Lemma map_tr_term_typed (Sg : Signature) (αs βs : list Sg.(sg_ty))
