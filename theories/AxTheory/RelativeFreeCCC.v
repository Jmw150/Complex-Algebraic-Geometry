(** Atom embedding: an algebraic term [t : Term Sg] becomes the
    structurally identical [AxTerm Sg] (variables go to [ax_var],
    function applications stay function applications). *)
Fixpoint atom {Sg : Signature} (t : Term Sg) : AxTerm Sg :=
  match t with
  | t_var i        => ax_var i
  | t_app f args   => ax_app_fn f (List.map atom args)
  end.

(** Atom embedding preserves typing.  An algebraic term [t] of sort
    [τ] in algebraic context [αs] becomes an Ax-term of type
    [ax_ground τ] in the matching Ax-context [List.map ax_ground αs]. *)
Lemma atom_preserves_type (Sg : Signature) (αs : list Sg.(sg_ty)) :
    forall t τ, HasType Sg αs t τ ->
                AxHasType Sg (List.map ax_ground αs) (atom t) (ax_ground τ).
Proof.
  fix IH 3.
  intros t τ Ht. destruct Ht as [n τ Hn | f args Hargs].
  - (* t_var n *)
    simpl. apply ax_ht_var.
    rewrite List.nth_error_map, Hn. reflexivity.
  - (* t_app f args *)
    simpl. apply ax_ht_fn.
    induction Hargs as [|a σ rest dom' Ha Hrest IHrest].
    + constructor.
    + constructor.
      * exact (IH a σ Ha).
      * exact IHrest.
Qed.

(** Iterated projections: [proj_at αs i M] produces a term of type
    [αs[i]] from [M : ax_iter_prod αs] via a chain of [ax_fst]/[ax_snd]. *)
Fixpoint proj_at {Sg : Signature} (αs : list (AxType Sg))
    (i : nat) (M : AxTerm Sg) : AxTerm Sg :=
  match αs with
  | []        => M  (* unreachable: i out of range *)
  | [_]       => M  (* αs is a singleton: ax_iter_prod = α; M itself is the value *)
  | _ :: αs'  =>
      match i with
      | 0   => ax_fst M
      | S j => proj_at αs' j (ax_snd M)
      end
  end.

Lemma proj_at_typed (Sg : Signature) (Γ : list (AxType Sg)) :
    forall (αs : list (AxType Sg)) (i : nat) (τ : AxType Sg) (M : AxTerm Sg),
      List.nth_error αs i = Some τ ->
      AxHasType Sg Γ M (ax_iter_prod αs) ->
      AxHasType Sg Γ (proj_at αs i M) τ.
Proof.
  intro αs.
  induction αs as [|α αs' IH]; intros i τ M Hi HM.
  - destruct i; discriminate.
  - (* αs = α :: αs' *)
    destruct αs' as [|α' αs''].
    + (* αs = [α] *)
      destruct i as [|i].
      * simpl in Hi. injection Hi as <-. simpl. exact HM.
      * destruct i; discriminate.
    + (* αs = α :: α' :: αs'' *)
      change (proj_at (α :: α' :: αs'') i M) with
        (match i with
         | 0   => ax_fst M
         | S j => proj_at (α' :: αs'') j (ax_snd M)
         end).
      destruct i as [|j].
      * simpl in Hi. injection Hi as <-.
        apply (ax_ht_fst Sg Γ M α (ax_iter_prod (α' :: αs''))).
        change (ax_iter_prod (α :: α' :: αs''))
          with (ax_prod α (ax_iter_prod (α' :: αs''))) in HM.
        exact HM.
      * simpl in Hi.
        apply IH.
        -- exact Hi.
        -- apply (ax_ht_snd Sg Γ M α (ax_iter_prod (α' :: αs''))).
           change (ax_iter_prod (α :: α' :: αs''))
             with (ax_prod α (ax_iter_prod (α' :: αs''))) in HM.
           exact HM.
Qed.

(** [unpack αs] is the substitution
    [proj_at (map ax_ground αs) 0 (ax_var 0); ...; proj_at ... (n-1) (ax_var 0)]
    where n = length αs.  It maps the singleton context [I_obj αs] to
    the multi-context [List.map ax_ground αs]. *)
Definition unpack {Sg : Signature} (αs : list Sg.(sg_ty)) : list (AxTerm Sg) :=
  List.map (fun i => proj_at (List.map ax_ground αs) i (ax_var 0))
           (List.seq 0 (length αs)).

Lemma unpack_typed (Sg : Signature) (αs : list Sg.(sg_ty)) :
    Forall2 (AxHasType Sg [I_obj αs]) (unpack αs) (List.map ax_ground αs).
Proof.
  unfold unpack, I_obj.
  set (M0 := ax_var 0 : AxTerm Sg).
  set (sortlist := List.map ax_ground αs).
  assert (HM0 : AxHasType Sg [ax_iter_prod sortlist] M0 (ax_iter_prod sortlist)).
  { apply ax_ht_var. reflexivity. }
  assert (Hlen : length αs = length sortlist).
  { unfold sortlist. symmetry. apply List.length_map. }
  rewrite Hlen.
  (* Generalize over the sublist viewpoint. *)
  assert (Hgen : forall (L0 : list (AxType Sg)) (k : nat),
             (forall i τ, List.nth_error L0 i = Some τ ->
                          List.nth_error sortlist (k + i) = Some τ) ->
             Forall2 (AxHasType Sg [ax_iter_prod sortlist])
                     (List.map (fun i => proj_at sortlist i M0)
                               (List.seq k (length L0)))
                     L0).
  { induction L0 as [|τ L0' IH'].
    - intros k _. constructor.
    - intros k Hnth.
      simpl. constructor.
      + apply (proj_at_typed Sg [ax_iter_prod sortlist] sortlist k τ M0).
        * specialize (Hnth 0 τ eq_refl). rewrite Nat.add_0_r in Hnth. exact Hnth.
        * exact HM0.
      + apply (IH' (S k)).
        intros i τ' Hi. specialize (Hnth (S i) τ' Hi).
        replace (S k + i) with (k + S i) by lia.
        exact Hnth. }
  apply (Hgen sortlist 0).
  intros i τ Hi. simpl. exact Hi.
Qed.

(** Iterated pair: matches the structure of [ax_iter_prod]. *)
Fixpoint pack {Sg : Signature} (Ms : list (AxTerm Sg)) : AxTerm Sg :=
  match Ms with
  | []           => ax_tt
  | [M]          => M
  | M :: Ms'     => ax_pair M (pack Ms')
  end.

Lemma pack_typed (Sg : Signature) (Γ : list (AxType Sg)) :
    forall (Ms : list (AxTerm Sg)) (αs : list (AxType Sg)),
      Forall2 (AxHasType Sg Γ) Ms αs ->
      AxHasType Sg Γ (pack Ms) (ax_iter_prod αs).
Proof.
  intro Ms.
  induction Ms as [|M Ms' IH]; intros αs Hf.
  - inversion Hf. simpl. apply ax_ht_tt.
  - inversion Hf as [|M0 α Ms0 αs' HM Hf' EM Eα]; subst.
    destruct Ms' as [|M' Ms''].
    + (* Ms = [M] *)
      inversion Hf'. subst. simpl. exact HM.
    + (* Ms = M :: M' :: Ms'' *)
      inversion Hf' as [|M0' α' Ms0' αs'' HM' Hf'' EM' Eα']; subst.
      change (pack (M :: M' :: Ms''))
        with (ax_pair M (pack (M' :: Ms''))).
      change (ax_iter_prod (α :: α' :: αs''))
        with (ax_prod α (ax_iter_prod (α' :: αs''))).
      apply ax_ht_pair.
      * exact HM.
      * apply IH. constructor; assumption.
Qed.

(** Translation of one algebraic term to the singleton Ax-context. *)
Definition tr_term {Sg : Signature} (αs : list Sg.(sg_ty))
    (t : Term Sg) : AxTerm Sg :=
  ax_subst (unpack αs) (atom t).

Lemma tr_term_typed (Sg : Signature) (αs : list Sg.(sg_ty))
    (t : Term Sg) (τ : Sg.(sg_ty)) :
    HasType Sg αs t τ ->
    AxHasType Sg [I_obj αs] (tr_term αs t) (ax_ground τ).
Proof.
  intro Ht. unfold tr_term.
  apply (ax_subst_preserves_type Sg [I_obj αs] (List.map ax_ground αs)
                                 (unpack αs) (unpack_typed Sg αs)).
  apply atom_preserves_type. exact Ht.
Qed.

(** Translation of a list of algebraic terms (= packing). *)
Definition tr_terms {Sg : Signature} (αs : list Sg.(sg_ty))
    (ts : list (Term Sg)) : AxTerm Sg :=
  pack (List.map (tr_term αs) ts).

Lemma tr_terms_typed (Sg : Signature) (αs βs : list Sg.(sg_ty))
    (ts : list (Term Sg)) :
    Forall2 (HasType Sg αs) ts βs ->
    AxHasType Sg [I_obj αs] (tr_terms αs ts) (I_obj βs).
Proof.
  intro Hts. unfold tr_terms, I_obj.
  apply pack_typed.
  induction Hts as [|t τ ts' βs' Ht Hts' IH].
  - constructor.
  - simpl. constructor.
    + exact (tr_term_typed Sg αs t τ Ht).
    + exact IH.
Qed.

