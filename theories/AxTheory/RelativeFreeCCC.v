(** Iterated unpack of [M] via [αs]: list of projections of [M]. *)
Definition unpack_of {Sg : Signature} (αs : list (AxType Sg))
    (M : AxTerm Sg) : list (AxTerm Sg) :=
  List.map (fun i => proj_at αs i M) (List.seq 0 (length αs)).

Lemma map_seq_S {A : Type} (f : nat -> A) (start n : nat) :
    List.map f (List.seq (S start) n) =
    List.map (fun i => f (S i)) (List.seq start n).
Proof.
  revert start. induction n as [|n IH]; intro start.
  - reflexivity.
  - simpl. f_equal. apply IH.
Qed.

(** Unpack of [α :: α' :: αs'] applied to M peels off [ax_fst M] then
    proceeds with [α' :: αs'] on [ax_snd M]. *)
Lemma unpack_of_cons_cons (Sg : Signature) (α α' : AxType Sg)
    (αs : list (AxType Sg)) (M : AxTerm Sg) :
    unpack_of (α :: α' :: αs) M =
    ax_fst M :: unpack_of (α' :: αs) (ax_snd M).
Proof.
  unfold unpack_of.
  change (length (α :: α' :: αs)) with (S (length (α' :: αs))).
  change (List.seq 0 (S (length (α' :: αs))))
    with (0 :: List.seq 1 (length (α' :: αs))).
  cbn [List.map].
  change (proj_at (α :: α' :: αs) 0 M) with (ax_fst M).
  f_equal.
  rewrite map_seq_S.
  apply List.map_ext_in. intros i _.
  cbn [proj_at]. reflexivity.
Qed.

(** Pack-unpack identity (η-pair) for the non-singleton case. *)
Lemma pack_unpack_eta_nonempty (Sg : Signature) (Γ : list (AxType Sg)) :
    forall (αs : list (AxType Sg)) (M : AxTerm Sg),
      αs <> [] ->
      AxHasType Sg Γ M (ax_iter_prod αs) ->
      AxThEq Sg [] Γ (pack (unpack_of αs M)) M (ax_iter_prod αs).
Proof.
  intro αs.
  induction αs as [|α αs' IH]; intros M Hne HM.
  - exfalso. apply Hne. reflexivity.
  - destruct αs' as [|α' αs''].
    + (* αs = [α], singleton *)
      unfold unpack_of. simpl. apply axeq_refl. exact HM.
    + (* αs = α :: α' :: αs'' *)
      rewrite unpack_of_cons_cons.
      change (ax_iter_prod (α :: α' :: αs''))
        with (ax_prod α (ax_iter_prod (α' :: αs''))) in HM.
      change (ax_iter_prod (α :: α' :: αs''))
        with (ax_prod α (ax_iter_prod (α' :: αs''))).
      change (pack (ax_fst M :: unpack_of (α' :: αs'') (ax_snd M)))
        with (ax_pair (ax_fst M) (pack (unpack_of (α' :: αs'') (ax_snd M)))).
      apply (axeq_trans Sg [] Γ
               (ax_pair (ax_fst M) (pack (unpack_of (α' :: αs'') (ax_snd M))))
               (ax_pair (ax_fst M) (ax_snd M))
               M
               (ax_prod α (ax_iter_prod (α' :: αs'')))).
      * apply axeq_pair.
        -- apply axeq_refl.
           apply (ax_ht_fst Sg Γ M α (ax_iter_prod (α' :: αs''))).
           exact HM.
        -- apply IH.
           ++ discriminate.
           ++ apply (ax_ht_snd Sg Γ M α (ax_iter_prod (α' :: αs''))).
              exact HM.
      * apply axeq_eta_prod. exact HM.
Qed.

(** [tr_term αs (t_var i) = proj_at (map ax_ground αs) i (ax_var 0)]
    when [i < length αs]. *)
Lemma nth_error_seq_lt (start n i : nat) :
    (i < n)%nat ->
    List.nth_error (List.seq start n) i = Some (start + i).
Proof.
  revert start i. induction n as [|n IH]; intros start i Hi.
  - lia.
  - destruct i as [|i'].
    + simpl. f_equal. lia.
    + simpl. rewrite IH by lia. f_equal. lia.
Qed.

Lemma tr_term_var (Sg : Signature) (αs : list Sg.(sg_ty)) (i : nat) :
    (i < length αs)%nat ->
    tr_term αs (t_var i) =
    proj_at (List.map ax_ground αs) i (ax_var 0).
Proof.
  intro Hi. unfold tr_term, atom, unpack.
  cbn [ax_subst].
  rewrite List.nth_error_map.
  rewrite (nth_error_seq_lt 0 (length αs) i Hi). cbn.
  reflexivity.
Qed.

(** Map of tr_term over the identity substitution gives the unpack list. *)
Lemma tr_terms_id_sub (Sg : Signature) (αs : list Sg.(sg_ty)) :
    List.map (tr_term αs) (id_sub Sg (length αs)) =
    unpack_of (List.map ax_ground αs) (ax_var 0).
Proof.
  unfold id_sub, unpack_of.
  rewrite List.map_map.
  assert (Hlen : length αs = length (List.map ax_ground αs)).
  { rewrite List.length_map. reflexivity. }
  rewrite Hlen.
  apply List.map_ext_in. intros i Hi.
  apply List.in_seq in Hi. destruct Hi as [_ Hi].
  apply tr_term_var. rewrite List.length_map in Hi. exact Hi.
Qed.

(** I preserves identity.  The empty-context case ([αs = []]) requires
    unit-η in [AxThEq] which is NOT in Syntax.v; we fall back to
    [axcl_terminal_unique] (an Axiom in ClassifyingCategory.v that
    encapsulates exactly this missing rule).  The non-empty cases are
    discharged via the [η-pair] derivation [pack_unpack_eta_nonempty]. *)
Lemma I_fmap_id : forall {Sg : Signature} (αs : list Sg.(sg_ty)),
    I_map αs αs ((Cl Sg).(id) αs) = (AxCl Sg).(id) (I_obj αs).
Proof.
  intros Sg αs.
  destruct αs as [|α0 αs0].
  - (* αs = [], so I_obj αs = ax_unit and both sides land in
       AxCl Sg ⟦ ax_unit, ax_unit ⟧ — use terminal uniqueness. *)
    transitivity (axcl_to_unit Sg ax_unit).
    + apply axcl_terminal_unique.
    + symmetry. apply axcl_terminal_unique.
  - (* αs = α0 :: αs0, non-empty *)
    set (αs := α0 :: αs0).
    unfold I_map, I_map_rep.
    cbn [ClassifyingCat id cl_id clmor_terms].
    unfold axcl_id.
    apply mk_q_eq_iff.
    unfold AxClMor_eq, axcl_id_rep. cbn [axcl_term].
    unfold tr_terms.
    rewrite tr_terms_id_sub.
    unfold I_obj.
    assert (Hne : List.map ax_ground αs <> []).
    { unfold αs. simpl. discriminate. }
    apply pack_unpack_eta_nonempty.
    + exact Hne.
    + apply ax_ht_var. reflexivity.
Qed.

