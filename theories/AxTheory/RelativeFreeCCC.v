  - apply pack_unpack_eta_nonempty; [discriminate|exact HM].
Qed.

Lemma length_unpack_of_inline {Sg : Signature} (αs : list (AxType Sg)) (M : AxTerm Sg) :
    length (unpack_of αs M) = length αs.
Proof.
  unfold unpack_of. rewrite List.length_map, List.length_seq. reflexivity.
Qed.

(** [unpack_of] of a typed term is a typed substitution at αs. *)
Lemma unpack_of_typed (Sg : Signature) (Γ : list (AxType Sg))
    (αs : list (AxType Sg)) (M : AxTerm Sg) :
    AxHasType Sg Γ M (ax_iter_prod αs) ->
    Forall2 (AxHasType Sg Γ) (unpack_of αs M) αs.
Proof.
  intro HM. unfold unpack_of.
  assert (Hgen : forall (k : nat) (αs0 : list (AxType Sg)),
             (forall i τ, List.nth_error αs0 i = Some τ ->
                          List.nth_error αs (k + i) = Some τ) ->
             Forall2 (AxHasType Sg Γ)
                     (List.map (fun i => proj_at αs i M) (List.seq k (length αs0)))
                     αs0).
  { intros k αs0. revert k.
    induction αs0 as [|τ αs0' IH']; intros k Hnth.
    - simpl. constructor.
    - simpl. constructor.
      + apply (proj_at_typed Sg Γ αs k τ M).
        * specialize (Hnth 0 τ eq_refl). rewrite Nat.add_0_r in Hnth. exact Hnth.
        * exact HM.
      + apply (IH' (S k)).
        intros i τ' Hi'. specialize (Hnth (S i) τ' Hi').
        replace (S k + i) with (k + S i) by lia.
        exact Hnth. }
  apply (Hgen 0 αs).
  intros i τ Hi. simpl. exact Hi.
Qed.

(** Typing of the αs-block of forward projections. *)
Lemma block_typed_alpha (Sg : Signature) (αs βs : list Sg.(sg_ty)) :
    AxHasType Sg [I_obj (αs ++ βs)]
      (pack (List.map
              (fun i => proj_at (List.map ax_ground (αs ++ βs)) i (ax_var 0))
              (List.seq 0 (length αs))))
      (I_obj αs).
Proof.
  apply pack_typed.
  set (sortAB := List.map ax_ground (αs ++ βs)).
  set (sortA  := List.map ax_ground αs).
  unfold I_obj.
  assert (Hv0 : AxHasType Sg [I_obj (αs ++ βs)] (ax_var 0)
                  (ax_iter_prod sortAB)).
  { apply ax_ht_var. reflexivity. }
  assert (HlenA : length αs = length sortA).
  { unfold sortA. symmetry. apply List.length_map. }
  rewrite HlenA.
  assert (Hgen : forall (k : nat) (L0 : list (AxType Sg)),
             (forall i τ, List.nth_error L0 i = Some τ ->
                          List.nth_error sortAB (k + i) = Some τ) ->
             Forall2 (AxHasType Sg [I_obj (αs ++ βs)])
                     (List.map (fun i => proj_at sortAB i (ax_var 0))
                               (List.seq k (length L0)))
                     L0).
  { intros k L0. revert k.
    induction L0 as [|τ L0' IH']; intros k Hnth.
    - simpl. constructor.
    - simpl. constructor.
      + apply (proj_at_typed Sg [I_obj (αs ++ βs)] sortAB k τ (ax_var 0)).
        * specialize (Hnth 0 τ eq_refl). rewrite Nat.add_0_r in Hnth. exact Hnth.
        * exact Hv0.
      + apply (IH' (S k)).
        intros i τ' Hi'. specialize (Hnth (S i) τ' Hi').
        replace (S k + i) with (k + S i) by lia.
        exact Hnth. }
  apply (Hgen 0 sortA).
  intros i τ Hi.
  assert (HiA : i < length sortA).
  { apply List.nth_error_Some. intro Hcontra.
    rewrite Hi in Hcontra. discriminate. }
  unfold sortAB. rewrite List.map_app.
  fold sortA.
  rewrite List.nth_error_app1 by exact HiA.
  simpl. exact Hi.
Qed.

(** Typing of the βs-block of forward projections. *)
Lemma block_typed_beta (Sg : Signature) (αs βs : list Sg.(sg_ty)) :
    AxHasType Sg [I_obj (αs ++ βs)]
      (pack (List.map
              (fun i => proj_at (List.map ax_ground (αs ++ βs)) i (ax_var 0))
              (List.seq (length αs) (length βs))))
      (I_obj βs).
Proof.
  apply pack_typed.
  set (sortAB := List.map ax_ground (αs ++ βs)).
  set (sortB  := List.map ax_ground βs).
  unfold I_obj.
  assert (Hv0 : AxHasType Sg [I_obj (αs ++ βs)] (ax_var 0)
                  (ax_iter_prod sortAB)).
  { apply ax_ht_var. reflexivity. }
  assert (HlenB : length βs = length sortB).
  { unfold sortB. symmetry. apply List.length_map. }
  rewrite HlenB.
  assert (HlenA_sortA : length αs = length (List.map ax_ground αs)).
  { symmetry. apply List.length_map. }
  assert (Hgen : forall (k : nat) (L0 : list (AxType Sg)),
             (forall i τ, List.nth_error L0 i = Some τ ->
                          List.nth_error sortAB (length αs + k + i) = Some τ) ->
             Forall2 (AxHasType Sg [I_obj (αs ++ βs)])
                     (List.map (fun i => proj_at sortAB i (ax_var 0))
                               (List.seq (length αs + k) (length L0)))
                     L0).
  { intros k L0. revert k.
    induction L0 as [|τ L0' IH']; intros k Hnth.
    - simpl. constructor.
    - simpl. constructor.
      + apply (proj_at_typed Sg [I_obj (αs ++ βs)] sortAB
                 (length αs + k) τ (ax_var 0)).
        * specialize (Hnth 0 τ eq_refl). rewrite Nat.add_0_r in Hnth.
          exact Hnth.
        * exact Hv0.
      + replace (S (length αs + k)) with (length αs + S k) by lia.
        apply (IH' (S k)).
        intros i τ' Hi'. specialize (Hnth (S i) τ' Hi').
        replace (length αs + S k + i) with (length αs + k + S i) by lia.
        exact Hnth. }
  replace (length αs) with (length αs + 0) at 1 by lia.
  apply (Hgen 0 sortB).
  intros i τ Hi.
  unfold sortAB. rewrite List.map_app.
  rewrite List.nth_error_app2.
  - rewrite <- HlenA_sortA. replace (length αs + 0 + i - length αs) with i by lia.
    simpl. exact Hi.
  - rewrite <- HlenA_sortA. lia.
Qed.

(** Forward representative term. *)
Definition prod_iso_fwd_term {Sg : Signature} (αs βs : list Sg.(sg_ty))
    : AxTerm Sg :=
  let sortAB := List.map ax_ground (αs ++ βs) in
  ax_pair
    (pack (List.map (fun i => proj_at sortAB i (ax_var 0))
                    (List.seq 0 (length αs))))
    (pack (List.map (fun i => proj_at sortAB i (ax_var 0))
                    (List.seq (length αs) (length βs)))).

(** Backward representative term. *)
Definition prod_iso_bwd_term {Sg : Signature} (αs βs : list Sg.(sg_ty))
    : AxTerm Sg :=
  let sortA := List.map ax_ground αs in
  let sortB := List.map ax_ground βs in
  pack (unpack_of sortA (ax_fst (ax_var 0)) ++
        unpack_of sortB (ax_snd (ax_var 0))).

Lemma prod_iso_fwd_typed (Sg : Signature) (αs βs : list Sg.(sg_ty)) :
    AxHasType Sg [I_obj (αs ++ βs)]
      (prod_iso_fwd_term αs βs)
      (I_obj αs ×ax I_obj βs).
Proof.
  unfold prod_iso_fwd_term. apply ax_ht_pair.
  - apply block_typed_alpha.
  - apply block_typed_beta.
Qed.

Lemma prod_iso_bwd_typed (Sg : Signature) (αs βs : list Sg.(sg_ty)) :
    AxHasType Sg [I_obj αs ×ax I_obj βs]
      (prod_iso_bwd_term αs βs)
      (I_obj (αs ++ βs)).
Proof.
  unfold prod_iso_bwd_term, I_obj.
  rewrite List.map_app.
  apply pack_typed.
  apply List.Forall2_app.
  - apply unpack_of_typed.
    apply (ax_ht_fst Sg [I_obj αs ×ax I_obj βs] (ax_var 0)
             (I_obj αs) (I_obj βs)).
    apply ax_ht_var. reflexivity.
  - apply unpack_of_typed.
    apply (ax_ht_snd Sg [I_obj αs ×ax I_obj βs] (ax_var 0)
             (I_obj αs) (I_obj βs)).
    apply ax_ht_var. reflexivity.
Qed.

(** [AxClMor] representatives. *)
Definition prod_iso_fwd_rep {Sg : Signature} (αs βs : list Sg.(sg_ty))
    : AxClMor Sg (I_obj (αs ++ βs)) (I_obj αs ×ax I_obj βs) :=
  {| axcl_term  := prod_iso_fwd_term αs βs;
     axcl_typed := prod_iso_fwd_typed Sg αs βs |}.

Definition prod_iso_bwd_rep {Sg : Signature} (αs βs : list Sg.(sg_ty))
    : AxClMor Sg (I_obj αs ×ax I_obj βs) (I_obj (αs ++ βs)) :=
  {| axcl_term  := prod_iso_bwd_term αs βs;
     axcl_typed := prod_iso_bwd_typed Sg αs βs |}.

(** AxCl-level (quotient) morphisms. *)
Definition prod_iso_fwd {Sg : Signature} (αs βs : list Sg.(sg_ty))
    : AxCl Sg ⟦ I_obj (αs ++ βs), I_obj αs ×ax I_obj βs ⟧ :=
  mk_q (prod_iso_fwd_rep αs βs).

Definition prod_iso_bwd {Sg : Signature} (αs βs : list Sg.(sg_ty))
    : AxCl Sg ⟦ I_obj αs ×ax I_obj βs, I_obj (αs ++ βs) ⟧ :=
  mk_q (prod_iso_bwd_rep αs βs).

(** ** Helpers used by the round-trip equations *)

(** [ax_subst] commutes with [pack] (= map and reassemble). *)
Lemma ax_subst_pack (Sg : Signature) (sub : list (AxTerm Sg))
    (Ms : list (AxTerm Sg)) :
    ax_subst sub (pack Ms) =
    pack (List.map (ax_subst sub) Ms).
Proof.
  induction Ms as [|M Ms IH].
  - reflexivity.
  - destruct Ms as [|M' Ms''].
    + reflexivity.
    + change (pack (M :: M' :: Ms'')) with (ax_pair M (pack (M' :: Ms''))).
      cbn [ax_subst List.map].
      change (pack (ax_subst sub M :: ax_subst sub M'
                    :: List.map (ax_subst sub) Ms''))
        with (ax_pair (ax_subst sub M)
                      (pack (ax_subst sub M' :: List.map (ax_subst sub) Ms''))).
      f_equal.
      change (List.map (ax_subst sub) (M' :: Ms''))
        with (ax_subst sub M' :: List.map (ax_subst sub) Ms'') in IH.
      exact IH.
Qed.

(** [ax_subst] commutes with [unpack_of]. *)
Lemma ax_subst_unpack_of (Sg : Signature) (sub : list (AxTerm Sg))
    (αs : list (AxType Sg)) (M : AxTerm Sg) :
    List.map (ax_subst sub) (unpack_of αs M) =
    unpack_of αs (ax_subst sub M).
Proof.
  unfold unpack_of. rewrite List.map_map.
  apply List.map_ext. intro i.
  apply ax_subst_proj_at.
Qed.

(** Indexed pointwise-AxThEq predicate (avoids [combine] reduction issues). *)
Definition list_axeq_pointwise {Sg : Signature} (Γ : list (AxType Sg))
    (Ms Ms' : list (AxTerm Sg)) (γs : list (AxType Sg)) : Prop :=
  forall (i : nat) (τ : AxType Sg) (M M' : AxTerm Sg),
    List.nth_error γs i = Some τ ->
    List.nth_error Ms i = Some M ->
    List.nth_error Ms' i = Some M' ->
    AxThEq Sg [] Γ M M' τ.

(** Pack-congruence (cleaned formulation). *)
Lemma pack_cong (Sg : Signature) (Γ : list (AxType Sg)) :
    forall (Ms Ms' : list (AxTerm Sg)) (γs : list (AxType Sg)),
      Forall2 (AxHasType Sg Γ) Ms γs ->
      Forall2 (AxHasType Sg Γ) Ms' γs ->
      list_axeq_pointwise Γ Ms Ms' γs ->
      AxThEq Sg [] Γ (pack Ms) (pack Ms') (ax_iter_prod γs).
Proof.
  intros Ms.
  induction Ms as [|M Ms IH]; intros Ms' γs HMs HMs' Hcong.
  - inversion HMs; subst.
    inversion HMs'; subst.
    cbn [pack ax_iter_prod].
    apply axeq_refl. apply ax_ht_tt.
  - destruct Ms' as [|M' Ms''].
    + apply Forall2_length in HMs'. simpl in HMs'.
      apply Forall2_length in HMs. simpl in HMs. lia.
    + inversion HMs as [|? γ ? γs0 HM Hrest E1 E2]; subst.
      inversion HMs' as [|? ? ? ? HM' Hrest']; subst.
      destruct Ms as [|M2 Ms2].
      * (* Ms = [M], so γs0 = []. *)
        inversion Hrest. subst γs0.
        destruct Ms'' as [|M2' Ms2'];
          [|apply Forall2_length in Hrest'; simpl in Hrest'; lia].
        cbn [pack ax_iter_prod].
        apply (Hcong 0 γ M M'); reflexivity.
      * destruct Ms'' as [|M2' Ms2'].
        { apply Forall2_length in Hrest'. simpl in Hrest'.
          apply Forall2_length in Hrest. simpl in Hrest. lia. }
        change (pack (M :: M2 :: Ms2)) with (ax_pair M (pack (M2 :: Ms2))).
        change (pack (M' :: M2' :: Ms2')) with (ax_pair M' (pack (M2' :: Ms2'))).
        inversion Hrest as [|? γ_first ? γs1 HM2 Hrest2 E3 E4]; subst.
        inversion Hrest' as [|? ? ? ? HM2' Hrest2']; subst.
        change (ax_iter_prod (γ :: γ_first :: γs1))
          with (ax_prod γ (ax_iter_prod (γ_first :: γs1))).
        apply axeq_pair.
        -- apply (Hcong 0 γ M M'); reflexivity.
        -- apply IH with (γs := γ_first :: γs1).
           ++ constructor; assumption.
           ++ constructor; assumption.
           ++ intros i τ X X' Hτi HXi HX'i.
              apply (Hcong (S i) τ X X'); cbn; assumption.
Qed.

(** ** Round-trip equations *)

(** [bwd ∘ fwd ~~ id] in AxThEq.

    Substituting [prod_iso_fwd_term αs βs] for var 0 in
    [prod_iso_bwd_term αs βs] yields
    [pack (unpack_of sortA (ax_fst FWD) ++ unpack_of sortB (ax_snd FWD))]
    where FWD = ax_pair PA PB.  By β-pair, this AxThEq-rewrites to
    [pack (unpack_of sortA PA ++ unpack_of sortB PB)].  Each block then
    AxThEq-rewrites pointwise (via [proj_at_pack] applied to PA, PB)
    to [unpack_of sortAB (ax_var 0)] (after combining sortA ++ sortB =
    sortAB by [List.map_app]).  Finally [pack_unpack_eta] on
    [ax_var 0 : I_obj (αs ++ βs)] closes to [ax_var 0]. *)

Lemma prod_iso_bwd_fwd_eq (Sg : Signature) (αs βs : list Sg.(sg_ty)) :
    AxThEq Sg [] [I_obj (αs ++ βs)]
      (ax_subst [prod_iso_fwd_term αs βs] (prod_iso_bwd_term αs βs))
      (ax_var 0)
      (I_obj (αs ++ βs)).
Proof.
  set (Γ := [I_obj (αs ++ βs)]).
  set (sortA := List.map ax_ground αs).
  set (sortB := List.map ax_ground βs).
  set (sortAB := List.map ax_ground (αs ++ βs)).
  assert (HsortAB : sortA ++ sortB = sortAB).
  { unfold sortA, sortB, sortAB. rewrite List.map_app. reflexivity. }
  assert (HlenA : length αs = length sortA).
  { unfold sortA. symmetry. apply List.length_map. }
  assert (HlenB : length βs = length sortB).
  { unfold sortB. symmetry. apply List.length_map. }
  set (PA := pack (List.map (fun i => proj_at sortAB i (ax_var 0))
                            (List.seq 0 (length αs)))).
  set (PB := pack (List.map (fun i => proj_at sortAB i (ax_var 0))
                            (List.seq (length αs) (length βs)))).
  assert (HPA : AxHasType Sg Γ PA (I_obj αs)).
  { unfold PA. apply block_typed_alpha. }
  assert (HPB : AxHasType Sg Γ PB (I_obj βs)).
  { unfold PB. apply block_typed_beta. }
  assert (Hv0 : AxHasType Sg Γ (ax_var 0) (ax_iter_prod sortAB)).
  { apply ax_ht_var. reflexivity. }
  (* Distribute ax_subst through prod_iso_bwd_term. *)
  unfold prod_iso_bwd_term.
  rewrite ax_subst_pack.
  rewrite List.map_app.
  rewrite !ax_subst_unpack_of.
  cbn [ax_subst].
  (* Goal: pack (unpack_of sortA (ax_fst FWD) ++ unpack_of sortB (ax_snd FWD))
            ~~ ax_var 0 *)
  (* We use FWD = ax_pair PA PB definitionally. *)
  change (prod_iso_fwd_term αs βs) with (ax_pair PA PB).
  (* Step: rewrite via β-pair to ax_pair PA PB → PA, PB. *)
  apply (axeq_trans Sg [] Γ _
            (pack (unpack_of sortA PA ++ unpack_of sortB PB))
            (ax_var 0) (I_obj (αs ++ βs))).
  - (* β-pair lift. Use ax_subst_cong with template
       [pack (unpack_of sortA (ax_var 1) ++ unpack_of sortB (ax_var 0))]
       and substitutions [ax_fst (ax_pair PA PB); ax_snd (ax_pair PA PB)]
       vs [PA; PB]. *)
    set (T := pack (unpack_of sortA (ax_var 0) ++ unpack_of sortB (ax_var 1))).
    assert (HT : AxHasType Sg [I_obj αs; I_obj βs] T (ax_iter_prod (sortA ++ sortB))).
    { unfold T. apply pack_typed. apply List.Forall2_app.
      - apply unpack_of_typed.
        apply ax_ht_var. cbn. reflexivity.
      - apply unpack_of_typed.
        apply ax_ht_var. cbn. reflexivity. }
    set (F := ax_fst (ax_pair PA PB)).
    set (S := ax_snd (ax_pair PA PB)).
    assert (HF : AxThEq Sg [] Γ F PA (I_obj αs)).
    { apply (axeq_beta_fst Sg [] Γ PA PB (I_obj αs) (I_obj βs) HPA HPB). }
    assert (HS : AxThEq Sg [] Γ S PB (I_obj βs)).
    { apply (axeq_beta_snd Sg [] Γ PA PB (I_obj αs) (I_obj βs) HPA HPB). }
    assert (HFt : AxHasType Sg Γ F (I_obj αs)).
    { unfold F. apply (ax_ht_fst Sg Γ (ax_pair PA PB) (I_obj αs) (I_obj βs)).
      apply ax_ht_pair; assumption. }
    assert (HSt : AxHasType Sg Γ S (I_obj βs)).
    { unfold S. apply (ax_ht_snd Sg Γ (ax_pair PA PB) (I_obj αs) (I_obj βs)).
      apply ax_ht_pair; assumption. }
    apply (axeq_trans Sg [] Γ _ (ax_subst [F; S] T) _ _).
    { unfold T. rewrite ax_subst_pack, List.map_app, !ax_subst_unpack_of.
      simpl ax_subst. apply axeq_refl.
      change (I_obj (αs ++ βs)) with (ax_iter_prod sortAB).
      rewrite <- HsortAB.
      apply pack_typed. apply List.Forall2_app.
      - apply unpack_of_typed. exact HFt.
      - apply unpack_of_typed. exact HSt. }
    apply (axeq_trans Sg [] Γ _ (ax_subst [PA; PB] T) _ _).
    2: { unfold T. rewrite ax_subst_pack, List.map_app, !ax_subst_unpack_of.
         simpl ax_subst. apply axeq_refl.
         change (I_obj (αs ++ βs)) with (ax_iter_prod sortAB).
         rewrite <- HsortAB.
         apply pack_typed. apply List.Forall2_app.
         - apply unpack_of_typed. exact HPA.
         - apply unpack_of_typed. exact HPB. }
    change (I_obj (αs ++ βs)) with (ax_iter_prod sortAB).
    rewrite <- HsortAB.
    apply (ax_subst_cong Sg [] Γ [I_obj αs; I_obj βs]
             [F; S] [PA; PB] T T (ax_iter_prod (sortA ++ sortB))).
    + constructor; [exact HFt|]. constructor; [exact HSt|]. constructor.
    + constructor; [exact HPA|]. constructor; [exact HPB|]. constructor.
    + intros k τ s s' Hτk Hsk Hs'k.
      destruct k as [|k]; cbn in *.
      * injection Hτk as <-. injection Hsk as <-. injection Hs'k as <-. exact HF.
      * destruct k as [|k]; cbn in *.
        -- injection Hτk as <-. injection Hsk as <-. injection Hs'k as <-. exact HS.
        -- destruct k; discriminate.
    + apply axeq_refl. exact HT.
  - (* pack(unpack_of sortA PA ++ unpack_of sortB PB) ~~ ax_var 0.
       Goal: AxThEq [] Γ (pack ...) (ax_var 0) (I_obj (αs ++ βs)). *)
    change (I_obj (αs ++ βs)) with (ax_iter_prod sortAB).
    rewrite <- HsortAB.
    apply (axeq_trans Sg [] Γ _
              (pack (unpack_of (sortA ++ sortB) (ax_var 0)))
              (ax_var 0) (ax_iter_prod (sortA ++ sortB))).
    + (* pack of (sortA-PA-projs ++ sortB-PB-projs) ~~ pack(unpack_of sortAB v0)
         pointwise via proj_at_pack. *)
      apply pack_cong with (γs := sortA ++ sortB).
      * apply List.Forall2_app.
        -- apply unpack_of_typed. exact HPA.
        -- apply unpack_of_typed. exact HPB.
      * unfold unpack_of.
        rewrite List.length_app.
        rewrite List.seq_app.
        rewrite List.map_app.
        apply List.Forall2_app.
        -- (* αs-block of unpack_of sortAB v0 *)
           assert (Hgen : forall (k : nat) (L0 : list (AxType Sg)),
                     (forall j τ, List.nth_error L0 j = Some τ ->
                                  List.nth_error sortAB (k + j) = Some τ) ->
                     Forall2 (AxHasType Sg Γ)
                             (List.map (fun j => proj_at sortAB j (ax_var 0))
                                       (List.seq k (length L0)))
                             L0).
           { intros k L0. revert k.
             induction L0 as [|τ L0' IH']; intros k Hnth.
             - simpl. constructor.
             - simpl. constructor.
               + apply (proj_at_typed Sg Γ sortAB k τ (ax_var 0)).
                 * specialize (Hnth 0 τ eq_refl). rewrite Nat.add_0_r in Hnth.
                   exact Hnth.
                 * exact Hv0.
               + apply (IH' (S k)).
                 intros j τ' Hj. specialize (Hnth (S j) τ' Hj).
                 replace (S k + j) with (k + S j) by lia. exact Hnth. }
           rewrite HsortAB.
           apply (Hgen 0 sortA).
           intros j τ Hj.
           assert (HjA : j < length sortA).
           { apply List.nth_error_Some. intro Hc. rewrite Hj in Hc. discriminate. }
           rewrite <- HsortAB.
           rewrite List.nth_error_app1 by exact HjA. exact Hj.
        -- (* βs-block of unpack_of sortAB v0 *)
           assert (Hgen : forall (k : nat) (L0 : list (AxType Sg)),
                     (forall j τ, List.nth_error L0 j = Some τ ->
                                  List.nth_error sortAB (length sortA + k + j) = Some τ) ->
                     Forall2 (AxHasType Sg Γ)
                             (List.map (fun j => proj_at sortAB j (ax_var 0))
                                       (List.seq (length sortA + k) (length L0)))
                             L0).
           { intros k L0. revert k.
             induction L0 as [|τ L0' IH']; intros k Hnth.
             - simpl. constructor.
             - simpl. constructor.
               + apply (proj_at_typed Sg Γ sortAB (length sortA + k) τ (ax_var 0)).
                 * specialize (Hnth 0 τ eq_refl). rewrite Nat.add_0_r in Hnth.
                   exact Hnth.
                 * exact Hv0.
               + replace (S (length sortA + k)) with (length sortA + S k) by lia.
                 apply (IH' (S k)).
                 intros j τ' Hj. specialize (Hnth (S j) τ' Hj).
                 replace (length sortA + S k + j) with (length sortA + k + S j) by lia.
                 exact Hnth. }
           rewrite HsortAB.
           replace (0 + length sortA) with (length sortA + 0) by lia.
           apply (Hgen 0 sortB).
           intros j τ Hj.
           assert (HjB : j < length sortB).
           { apply List.nth_error_Some. intro Hc. rewrite Hj in Hc. discriminate. }
           rewrite <- HsortAB.
           rewrite List.nth_error_app2 by lia.
           replace (length sortA + 0 + j - length sortA) with j by lia.
           exact Hj.
      * intros i τ X X' Hτi HXi HX'i.
        (* X = (unpack_of sortA PA ++ unpack_of sortB PB)[i]
           X' = unpack_of (sortA ++ sortB) (ax_var 0) [i] *)
        rewrite List.nth_error_app in HXi.
        rewrite length_unpack_of_inline in HXi.
        destruct (Nat.ltb_spec0 i (length sortA)) as [HiA | HiA].
        -- (* αs-block *)
           unfold unpack_of in HXi.
           rewrite List.nth_error_map in HXi.
           rewrite nth_error_seq_lt in HXi by exact HiA.
           cbn in HXi. injection HXi as <-.
           unfold unpack_of in HX'i.
           rewrite List.nth_error_map in HX'i.
           rewrite nth_error_seq_lt in HX'i.
           2: { rewrite List.length_app. lia. }
           cbn in HX'i. injection HX'i as <-.
           assert (Hτi_AB : List.nth_error sortAB i = Some τ).
           { rewrite <- HsortAB.
             rewrite List.nth_error_app1 in Hτi by lia.
             rewrite List.nth_error_app1 by exact HiA. exact Hτi. }
           (* Goal: AxThEq Γ (proj_at sortA i PA) (proj_at sortAB i (ax_var 0)) τ.
              Use proj_at_pack on PA. *)
           rewrite List.nth_error_app1 in Hτi by lia.
           assert (HMs_PA : Forall2 (AxHasType Sg Γ)
                              (List.map (fun j => proj_at sortAB j (ax_var 0))
                                        (List.seq 0 (length αs)))
                              sortA).
           { (* Inline argument matching block_typed_alpha. *)
             assert (Hgen : forall (k : nat) (L0 : list (AxType Sg)),
                       (forall j τ', List.nth_error L0 j = Some τ' ->
                                    List.nth_error sortAB (k + j) = Some τ') ->
                       Forall2 (AxHasType Sg Γ)
                               (List.map (fun j => proj_at sortAB j (ax_var 0))
                                         (List.seq k (length L0)))
                               L0).
             { intros k L0. revert k.
               induction L0 as [|τ' L0' IH']; intros k Hnth.
               - simpl. constructor.
               - simpl. constructor.
                 + apply (proj_at_typed Sg Γ sortAB k τ' (ax_var 0)).
                   * specialize (Hnth 0 τ' eq_refl). rewrite Nat.add_0_r in Hnth.
                     exact Hnth.
                   * exact Hv0.
                 + apply (IH' (S k)).
                   intros j τ'' Hj'. specialize (Hnth (S j) τ'' Hj').
                   replace (S k + j) with (k + S j) by lia. exact Hnth. }
             rewrite HlenA.
             apply (Hgen 0 sortA).
             intros j τ' Hj'.
             assert (HjA' : j < length sortA).
             { apply List.nth_error_Some. intro Hc. rewrite Hj' in Hc. discriminate. }
             rewrite <- HsortAB. rewrite List.nth_error_app1 by exact HjA'. exact Hj'. }
           change PA with (pack (List.map
                            (fun j => proj_at sortAB j (ax_var 0))
                            (List.seq 0 (length αs)))).
           rewrite HlenA in *.
           assert (Hpos_in_PAlist :
             List.nth_error
               (List.map (fun j => proj_at sortAB j (ax_var 0))
                         (List.seq 0 (length sortA))) i =
             Some (proj_at sortAB i (ax_var 0))).
           { rewrite List.nth_error_map. rewrite nth_error_seq_lt by exact HiA.
             cbn. reflexivity. }
           rewrite <- HsortAB in *.
           apply (proj_at_pack Sg Γ sortA
                    (List.map (fun j => proj_at (sortA ++ sortB) j (ax_var 0))
                              (List.seq 0 (length sortA))) i
                    (proj_at (sortA ++ sortB) i (ax_var 0)) τ
                    HMs_PA Hpos_in_PAlist Hτi).
        -- (* βs-block *)
           apply Nat.nlt_ge in HiA.
           assert (HiBspec : i - length sortA < length sortB).
           { rewrite List.nth_error_app2 in Hτi by exact HiA.
             apply List.nth_error_Some. rewrite Hτi. discriminate. }
           unfold unpack_of in HXi.
           rewrite List.nth_error_map in HXi.
           rewrite nth_error_seq_lt in HXi by exact HiBspec.
           cbn in HXi. injection HXi as <-.
           (* HX'i: nth_error (map(...) (seq 0 (|sortA|+|sortB|))) i = Some X' *)
           unfold unpack_of in HX'i.
           rewrite List.nth_error_map in HX'i.
           rewrite nth_error_seq_lt in HX'i.
           2: { apply List.nth_error_Some. rewrite Hτi. discriminate. }
           cbn in HX'i. injection HX'i as <-.
           (* HiBspec already in context from earlier. *)
           rewrite List.nth_error_app2 in Hτi by exact HiA.
           (* Goal: AxThEq (proj_at sortB (i-|sortA|) PB) (proj_at sortAB i v0) τ. *)
           change PB with (pack (List.map
                            (fun j => proj_at sortAB j (ax_var 0))
                            (List.seq (length αs) (length βs)))).
           assert (HMs_PB : Forall2 (AxHasType Sg Γ)
                              (List.map (fun j => proj_at sortAB j (ax_var 0))
                                        (List.seq (length αs) (length βs)))
                              sortB).
           { assert (Hgen : forall (k : nat) (L0 : list (AxType Sg)),
                       (forall j τ', List.nth_error L0 j = Some τ' ->
                                    List.nth_error sortAB (length αs + k + j) = Some τ') ->
                       Forall2 (AxHasType Sg Γ)
                               (List.map (fun j => proj_at sortAB j (ax_var 0))
                                         (List.seq (length αs + k) (length L0)))
                               L0).
             { intros k L0. revert k.
               induction L0 as [|τ' L0' IH']; intros k Hnth.
               - simpl. constructor.
               - simpl. constructor.
                 + apply (proj_at_typed Sg Γ sortAB (length αs + k) τ' (ax_var 0)).
                   * specialize (Hnth 0 τ' eq_refl). rewrite Nat.add_0_r in Hnth.
                     exact Hnth.
                   * exact Hv0.
                 + replace (S (length αs + k)) with (length αs + S k) by lia.
                   apply (IH' (S k)).
                   intros j τ'' Hj'. specialize (Hnth (S j) τ'' Hj').
                   replace (length αs + S k + j) with (length αs + k + S j) by lia.
                   exact Hnth. }
             rewrite HlenB.
             replace (length αs) with (length αs + 0) at 1 by lia.
             apply (Hgen 0 sortB).
             intros j τ' Hj'.
             assert (HjB' : j < length sortB).
             { apply List.nth_error_Some. intro Hc. rewrite Hj' in Hc. discriminate. }
             rewrite <- HsortAB. rewrite List.nth_error_app2 by lia.
             replace (length αs + 0 + j - length sortA) with j by lia. exact Hj'. }
           assert (Hpos_in_PBlist :
             List.nth_error
               (List.map (fun j => proj_at sortAB j (ax_var 0))
                         (List.seq (length αs) (length βs)))
               (i - length sortA) =
             Some (proj_at sortAB i (ax_var 0))).
           { rewrite List.nth_error_map.
             rewrite nth_error_seq_lt.
             - cbn. f_equal. f_equal. lia.
             - rewrite HlenB. exact HiBspec. }
           rewrite <- HsortAB in *.
           apply (proj_at_pack Sg Γ sortB
                    (List.map (fun j => proj_at (sortA ++ sortB) j (ax_var 0))
                              (List.seq (length αs) (length βs)))
                    (i - length sortA) (proj_at (sortA ++ sortB) i (ax_var 0)) τ
                    HMs_PB Hpos_in_PBlist Hτi).
    + apply pack_unpack_eta.
      rewrite HsortAB. exact Hv0.
Qed.

(** [fwd ∘ bwd ~~ id] in AxThEq.

    Sketch:
    Substituting [prod_iso_bwd_term αs βs] for var 0 in
    [prod_iso_fwd_term αs βs] yields
    [ax_pair (pack αs-block-of-projs of BWD) (pack βs-block-of-projs of BWD)].
    Each block-pack of projs of BWD AxThEq-rewrites (via [proj_at_pack]
    applied to BWD = pack (...append...)) to the corresponding
    [unpack_of sortA (ax_fst (ax_var 0))] and
    [unpack_of sortB (ax_snd (ax_var 0))] respectively.  Then by
    [pack_unpack_eta] on [ax_fst (ax_var 0) : I_obj αs] and
    [ax_snd (ax_var 0) : I_obj βs] (each typed in
    [I_obj αs ×ax I_obj βs]), we get
    [ax_pair (ax_fst (ax_var 0)) (ax_snd (ax_var 0))], which by
    [axeq_eta_prod] equals [ax_var 0]. *)

Lemma prod_iso_fwd_bwd_eq (Sg : Signature) (αs βs : list Sg.(sg_ty)) :
    AxThEq Sg [] [I_obj αs ×ax I_obj βs]
      (ax_subst [prod_iso_bwd_term αs βs] (prod_iso_fwd_term αs βs))
      (ax_var 0)
      (I_obj αs ×ax I_obj βs).
Proof.
  set (Γ := [I_obj αs ×ax I_obj βs]).
  set (sortA := List.map ax_ground αs).
  set (sortB := List.map ax_ground βs).
  set (sortAB := List.map ax_ground (αs ++ βs)).
  assert (HsortAB : sortA ++ sortB = sortAB).
  { unfold sortA, sortB, sortAB. rewrite List.map_app. reflexivity. }
  set (BWD := prod_iso_bwd_term αs βs).
  set (FST := ax_fst (ax_var 0) : AxTerm Sg).
  set (SND := ax_snd (ax_var 0) : AxTerm Sg).
  assert (HFST : AxHasType Sg Γ FST (I_obj αs)).
  { apply (ax_ht_fst Sg Γ (ax_var 0) (I_obj αs) (I_obj βs)).
    apply ax_ht_var. reflexivity. }
  assert (HSND : AxHasType Sg Γ SND (I_obj βs)).
  { apply (ax_ht_snd Sg Γ (ax_var 0) (I_obj αs) (I_obj βs)).
    apply ax_ht_var. reflexivity. }
  (* BWD = pack (unpack_of sortA FST ++ unpack_of sortB SND) *)
  assert (EBWD : BWD =
            pack (unpack_of sortA FST ++ unpack_of sortB SND)).
  { reflexivity. }
  (* Typing of pieces *)
  assert (HBWD_app : Forall2 (AxHasType Sg Γ)
            (unpack_of sortA FST ++ unpack_of sortB SND) (sortA ++ sortB)).
  { apply List.Forall2_app.
    - apply unpack_of_typed. exact HFST.
    - apply unpack_of_typed. exact HSND. }
  assert (HBWD_typed : AxHasType Sg Γ BWD (ax_iter_prod (sortA ++ sortB))).
  { rewrite EBWD. apply pack_typed. exact HBWD_app. }
  (* Goal LHS distributes through ax_subst on prod_iso_fwd_term. *)
  unfold prod_iso_fwd_term. cbn [ax_subst].
  rewrite !ax_subst_pack.
  rewrite !List.map_map.
  (* Each entry: ax_subst [BWD] (proj_at sortAB i (ax_var 0)) =
     proj_at sortAB i BWD by ax_subst_proj_at + cbn. *)
  erewrite (List.map_ext _ (fun i => proj_at sortAB i BWD)) by
    (intro i; rewrite ax_subst_proj_at; cbn [ax_subst]; reflexivity).
  match goal with
  | |- AxThEq _ _ _ (ax_pair _ ?B) _ _ =>
      replace B with (pack (List.map (fun i => proj_at sortAB i BWD)
                                     (List.seq (length αs) (length βs))))
  end.
  2: { f_equal. apply List.map_ext.
       intro i. rewrite ax_subst_proj_at. cbn [ax_subst]. reflexivity. }
  (* Step: this AxThEq-equals ax_pair FST SND. *)
  apply (axeq_trans Sg [] Γ _ (ax_pair FST SND) (ax_var 0)
            (I_obj αs ×ax I_obj βs)).
  - apply axeq_pair.
    + (* αs-block: pack(map (proj_at sortAB i BWD) (seq 0 |αs|)) ~~ FST. *)
      apply (axeq_trans Sg [] Γ _
                (pack (unpack_of sortA FST))
                FST (I_obj αs)).
      * (* pointwise: proj_at sortAB i BWD ~~ unpack_of sortA FST [i] for i < |αs|. *)
        unfold I_obj.
        assert (HlenA : length αs = length sortA).
        { unfold sortA. symmetry. apply List.length_map. }
        rewrite HlenA.
        apply pack_cong with (γs := sortA).
        -- (* LHS typing *)
           clear EBWD.
           rewrite HsortAB in HBWD_typed.
           assert (Hgen : forall (k : nat) (L0 : list (AxType Sg)),
                     (forall j τ, List.nth_error L0 j = Some τ ->
                                  List.nth_error sortAB (k + j) = Some τ) ->
                     Forall2 (AxHasType Sg Γ)
                             (List.map (fun j => proj_at sortAB j BWD)
                                       (List.seq k (length L0)))
                             L0).
           { intros k L0. revert k.
             induction L0 as [|τ L0' IH']; intros k Hnth.
             - simpl. constructor.
             - simpl. constructor.
               + apply (proj_at_typed Sg Γ sortAB k τ BWD).
                 * specialize (Hnth 0 τ eq_refl). rewrite Nat.add_0_r in Hnth.
                   exact Hnth.
                 * exact HBWD_typed.
               + apply (IH' (S k)).
                 intros j τ' Hj. specialize (Hnth (S j) τ' Hj).
                 replace (S k + j) with (k + S j) by lia. exact Hnth. }
           apply (Hgen 0 sortA).
           intros j τ Hj.
           assert (HjA : j < length sortA).
           { apply List.nth_error_Some. intro Hcontra.
             rewrite Hj in Hcontra. discriminate. }
           unfold sortAB. rewrite List.map_app.
           fold sortA.
           rewrite List.nth_error_app1 by exact HjA.
           simpl. exact Hj.
        -- apply unpack_of_typed. exact HFST.
        -- intros i τ X X' Hτi HXi HX'i.
           (* X = proj_at sortAB i BWD; X' = proj_at sortA i FST = unpack_of sortA FST [i]. *)
           assert (HiA : i < length sortA).
           { apply List.nth_error_Some. intro Hc. rewrite Hτi in Hc. discriminate. }
           assert (Eτ_ground : List.nth_error sortAB i = Some τ).
           { unfold sortAB. rewrite List.map_app. fold sortA.
             rewrite List.nth_error_app1 by exact HiA. exact Hτi. }
           (* HXi: nth_error of map f (seq 0 _) at i is Some (f i). *)
           rewrite List.nth_error_map in HXi.
           rewrite nth_error_seq_lt in HXi by exact HiA.
           cbn in HXi. injection HXi as <-.
           (* HX'i: unpack_of sortA FST is map(...) (seq 0 |sortA|). *)
           unfold unpack_of in HX'i.
           rewrite List.nth_error_map in HX'i.
           rewrite nth_error_seq_lt in HX'i by exact HiA.
           cbn in HX'i. injection HX'i as <-.
           (* Now: AxThEq (proj_at sortAB i BWD) (proj_at sortA i FST) τ.
              Use proj_at_pack on BWD = pack(unpack_of sortA FST ++ unpack_of sortB SND). *)
           rewrite EBWD.
           rewrite <- HsortAB.
           set (Ms_concat := unpack_of sortA FST ++ unpack_of sortB SND).
           assert (HMs_concat_typed :
             Forall2 (AxHasType Sg Γ) Ms_concat (sortA ++ sortB)).
           { unfold Ms_concat. apply List.Forall2_app.
             - apply unpack_of_typed. exact HFST.
             - apply unpack_of_typed. exact HSND. }
           assert (Hpos_in_concat :
             List.nth_error Ms_concat i = Some (proj_at sortA i FST)).
           { unfold Ms_concat. rewrite List.nth_error_app1.
             - unfold unpack_of.
               rewrite List.nth_error_map.
               rewrite nth_error_seq_lt by exact HiA.
               cbn. reflexivity.
             - rewrite length_unpack_of_inline. exact HiA. }
           apply (proj_at_pack Sg Γ (sortA ++ sortB) Ms_concat i
                    (proj_at sortA i FST) τ HMs_concat_typed Hpos_in_concat).
           rewrite List.nth_error_app1 by exact HiA. exact Hτi.
      * (* pack(unpack_of sortA FST) ~~ FST by pack_unpack_eta. *)
        apply pack_unpack_eta. unfold I_obj in HFST. exact HFST.
    + (* βs-block: similar *)
      apply (axeq_trans Sg [] Γ _
                (pack (unpack_of sortB SND))
                SND (I_obj βs)).
      * unfold I_obj.
        assert (HlenB : length βs = length sortB).
        { unfold sortB. symmetry. apply List.length_map. }
        rewrite HlenB.
        assert (HlenA_sortA : length αs = length sortA).
        { unfold sortA. symmetry. apply List.length_map. }
        apply pack_cong with (γs := sortB).
        -- (* LHS typing: each (proj_at sortAB (|αs|+i) BWD) typed at sortB[i]. *)
           rewrite HsortAB in HBWD_typed.
           assert (Hgen : forall (k : nat) (L0 : list (AxType Sg)),
                     (forall j τ, List.nth_error L0 j = Some τ ->
                                  List.nth_error sortAB (length αs + k + j) = Some τ) ->
                     Forall2 (AxHasType Sg Γ)
                             (List.map (fun j => proj_at sortAB j BWD)
                                       (List.seq (length αs + k) (length L0)))
                             L0).
           { intros k L0. revert k.
             induction L0 as [|τ L0' IH']; intros k Hnth.
             - simpl. constructor.
             - simpl. constructor.
               + apply (proj_at_typed Sg Γ sortAB (length αs + k) τ BWD).
                 * specialize (Hnth 0 τ eq_refl). rewrite Nat.add_0_r in Hnth.
                   exact Hnth.
                 * exact HBWD_typed.
               + replace (S (length αs + k)) with (length αs + S k) by lia.
                 apply (IH' (S k)).
                 intros j τ' Hj. specialize (Hnth (S j) τ' Hj).
                 replace (length αs + S k + j) with (length αs + k + S j) by lia.
                 exact Hnth. }
           replace (length αs) with (length αs + 0) at 1 by lia.
           apply (Hgen 0 sortB).
           intros j τ Hj.
           unfold sortAB. rewrite List.map_app. fold sortA.
           rewrite List.nth_error_app2.
           ++ rewrite <- HlenA_sortA.
              replace (length αs + 0 + j - length αs) with j by lia.
              simpl. exact Hj.
           ++ rewrite <- HlenA_sortA. lia.
        -- apply unpack_of_typed. exact HSND.
        -- intros i τ X X' Hτi HXi HX'i.
           assert (HiB : i < length sortB).
           { apply List.nth_error_Some. intro Hc. rewrite Hτi in Hc. discriminate. }
           assert (Eτ_ground : List.nth_error sortAB (length αs + i) = Some τ).
           { unfold sortAB. rewrite List.map_app. fold sortA.
             rewrite List.nth_error_app2 by lia.
             rewrite <- HlenA_sortA. replace (length αs + i - length αs) with i by lia.
             exact Hτi. }
           rewrite List.nth_error_map in HXi.
           rewrite nth_error_seq_lt in HXi by exact HiB.
           cbn in HXi. injection HXi as <-.
           unfold unpack_of in HX'i.
           rewrite List.nth_error_map in HX'i.
           rewrite nth_error_seq_lt in HX'i by exact HiB.
           cbn in HX'i. injection HX'i as <-.
           (* Goal: AxThEq (proj_at sortAB (length αs + i) BWD)
                          (proj_at sortB i SND) τ. *)
           rewrite EBWD.
           rewrite <- HsortAB.
           set (Ms_concat := unpack_of sortA FST ++ unpack_of sortB SND).
           assert (HMs_concat_typed :
             Forall2 (AxHasType Sg Γ) Ms_concat (sortA ++ sortB)).
           { unfold Ms_concat. apply List.Forall2_app.
             - apply unpack_of_typed. exact HFST.
             - apply unpack_of_typed. exact HSND. }
           assert (Hpos_in_concat :
             List.nth_error Ms_concat (length αs + i) = Some (proj_at sortB i SND)).
           { unfold Ms_concat. rewrite List.nth_error_app2.
             - rewrite length_unpack_of_inline.
               replace (length αs + i - length sortA) with i by lia.
               unfold unpack_of.
               rewrite List.nth_error_map.
               rewrite nth_error_seq_lt by exact HiB.
               cbn. reflexivity.
             - rewrite length_unpack_of_inline. lia. }
           apply (proj_at_pack Sg Γ (sortA ++ sortB) Ms_concat (length αs + i)
                    (proj_at sortB i SND) τ HMs_concat_typed Hpos_in_concat).
           rewrite List.nth_error_app2 by lia.
           rewrite <- HlenA_sortA. replace (length αs + i - length αs) with i by lia.
           exact Hτi.
      * apply pack_unpack_eta. unfold I_obj in HSND. exact HSND.
  - (* ax_pair (ax_fst v0) (ax_snd v0) ~~ v0 by axeq_eta_prod. *)
    apply axeq_eta_prod.
    apply ax_ht_var. reflexivity.
Qed.

(** Assemble the iso. *)

Lemma prod_iso_bwd_fwd_AxCl (Sg : Signature) (αs βs : list Sg.(sg_ty)) :
    prod_iso_bwd αs βs ∘ prod_iso_fwd αs βs =
    (AxCl Sg).(id) (I_obj (αs ++ βs)).
Proof.
  unfold prod_iso_bwd, prod_iso_fwd.
  cbn [AxCl comp].
  rewrite axcl_comp_mk.
  apply mk_q_eq_iff.
  unfold AxClMor_eq, axcl_comp_rep, axcl_id_rep, prod_iso_bwd_rep,
         prod_iso_fwd_rep. cbn [axcl_term].
  apply prod_iso_bwd_fwd_eq.
Qed.

Lemma prod_iso_fwd_bwd_AxCl (Sg : Signature) (αs βs : list Sg.(sg_ty)) :
    prod_iso_fwd αs βs ∘ prod_iso_bwd αs βs =
    (AxCl Sg).(id) (I_obj αs ×ax I_obj βs).
Proof.
  unfold prod_iso_bwd, prod_iso_fwd.
  cbn [AxCl comp].
  rewrite axcl_comp_mk.
  apply mk_q_eq_iff.
  unfold AxClMor_eq, axcl_comp_rep, axcl_id_rep, prod_iso_bwd_rep,
         prod_iso_fwd_rep. cbn [axcl_term].
  apply prod_iso_fwd_bwd_eq.
Qed.

Definition I_prod_iso (Sg : Signature) (αs βs : list Sg.(sg_ty)) :
    @Isomorphism (AxCl Sg)
      (I_obj (αs ++ βs))
      (I_obj αs ×ax I_obj βs) :=
  {| iso_fwd     := prod_iso_fwd αs βs;
     iso_bwd     := prod_iso_bwd αs βs;
     iso_fwd_bwd := prod_iso_bwd_fwd_AxCl Sg αs βs;
     iso_bwd_fwd := prod_iso_fwd_bwd_AxCl Sg αs βs;
  |}.

(** Terminal iso: [I_obj [] = ax_unit] definitionally. *)

Definition I_term_iso (Sg : Signature) :
    @Isomorphism (AxCl Sg) (I_obj []) ax_unit :=
  @iso_refl (AxCl Sg) ax_unit.

