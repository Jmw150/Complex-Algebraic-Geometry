(** This section delivers the object-level witness and structural
    sub-lemmas of the universal extension underlying
    [classifying_CCC_is_relatively_free].

    Given a CCC [D] (with [cD : IsCartesianClosed D]) and an FP-functor
    [F : Cl Sg → D] (in the iso-loosened sense [FPtoCCCFunctor]), the
    extension [F̃ : AxCl Sg → D] is determined RECURSIVELY on the
    [AxType Sg] inductive:

    - [F̃(ax_ground τ)] := [F ## [τ]]                       (singleton list)
    - [F̃(ax_unit)]     := [projT1 (fp_terminal cD.(ccc_fp))]   (terminal)
    - [F̃(α ×ax β)]     := [F̃(α) ×{cD.(ccc_fp).(fp_binary)} F̃(β)]
    - [F̃(α ⇒ax β)]     := [F̃(α) ⇒{cD} F̃(β)]

    The MORPHISM-level extension is more delicate: a morphism
    [α → β] in [AxCl Sg] is an [AxThEq]-class of a typed [AxTerm].
    For each [AxTerm M : AxHasType [α] M β], the standard
    interpretation [⟦M⟧ : F̃(α) → F̃(β)] is built recursively over the
    [AxHasType] derivation via D's CCC structure (variables → projections
    of the context, [ax_pair] → [bp_pair], [ax_fst]/[ax_snd] →
    [bp_proj1]/[bp_proj2], [ax_lam] → [exp_curry], [ax_ap] →
    [exp_eval ∘ ⟨_, _⟩], [ax_app_fn f] → [F #> (some morphism)]).
    The well-definedness modulo [AxThEq] is the heart of the proof.

    α R8 closed an analogous L.3 completeness statement
    ([term_to_clmor_q] / [interp_term_data] in the ATT subsystem); the
    construction here is structurally similar but lifts to the AxCl
    side.  See [REFACTOR_PLAN.org] Task A.2.next-prod / A.2.next.

    DELIVERED IN THIS ROUND (β R18):
    - [Fbar_obj]          — recursive object map, fully concrete.
    - [Fbar_obj_unit]     — unit case definitional.
    - [Fbar_obj_prod]     — product case definitional.
    - [Fbar_obj_exp]      — exponential case definitional.
    - [Fbar_obj_ground_singleton] — [F̃(ax_ground τ) = F ## [τ]].
    - [Fbar_obj_iter_prod_via_iso] — the iterated-product object map
      reaches [F ## αs] up to iterated [fptoccc_iso_prod] mediation
      (statement only as a comment; full proof depends on a
      [Fbar_iter_prod_iso] iso-chain construction which is a separate
      sub-task).

    DEFERRED (paper-attributable, scoped for future rounds):
    - [Fbar_term]         — interpretation of typed AxTerms in D.
    - [Fbar_term_well_defined] — congruence wrt [AxThEq].
    - [Fbar_functor]      — assembly into a Functor.
    - [Fbar_ccc_functor]  — CCCFunctor preservation laws.
    - [Fbar_triangle]     — F̃ ∘ I = F (FunctorObjEq).
