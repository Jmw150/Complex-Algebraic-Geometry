(** ** FP-functor into a CCC

    A finite-product-preserving functor from a category with FP into a CCC. *)

Record FPtoCCCFunctor {C D : Category} (hC : HasFiniteProducts C)
    (cD : IsCartesianClosed D) : Type := {
  fptoccc_funct    :> Functor C D;
  fptoccc_pres_term : fptoccc_funct ## (projT1 (@fp_terminal C hC)) =
                      projT1 (@fp_terminal D cD.(ccc_fp));
  fptoccc_pres_prod : forall A B,
                        fptoccc_funct ## (A ×{@fp_binary C hC} B) =
                        fptoccc_funct ## A ×{@fp_binary D cD.(ccc_fp)} fptoccc_funct ## B;
}.

Arguments fptoccc_funct    {C D hC cD}.
Arguments fptoccc_pres_term {C D hC cD}.
Arguments fptoccc_pres_prod {C D hC cD}.

(** ** Natural isomorphism (object-wise equality version)

    For our purposes we use pointwise equality of objects as the
    notion of "natural isomorphism" of functors.  A proper treatment
    would use NatTrans with iso components. *)

Definition FunctorObjEq {C D : Category} (F G : Functor C D) : Prop :=
  forall A, F ## A = G ## A.

(** ** Relatively free CCC *)

Record RelativelyFreeCCC
    {C : Category} (hC : HasFiniteProducts C)
    {TC : Category} (cTC : IsCartesianClosed TC)
    (J : FPtoCCCFunctor hC cTC) : Type := {

  (** Universal extension: any FP-functor F : C → D into a CCC
      extends to a CCC functor F̄ : TC → D *)
  rfccc_ext : forall {D : Category} (cD : IsCartesianClosed D)
                (F : FPtoCCCFunctor hC cD),
              CCCFunctor cTC cD;

  (** Comparison: F̄ ∘ J ≅ F on objects *)
  rfccc_cmp : forall {D : Category} (cD : IsCartesianClosed D)
                (F : FPtoCCCFunctor hC cD),
              FunctorObjEq
                (rfccc_ext cD F ∘F J.(fptoccc_funct))
                F.(fptoccc_funct);

  (** Uniqueness: if F̄' also extends F then F̄' ≅ F̄ *)
  rfccc_uniq : forall {D : Category} (cD : IsCartesianClosed D)
                 (F : FPtoCCCFunctor hC cD)
                 (Fbar' : CCCFunctor cTC cD),
               FunctorObjEq (Fbar' ∘F J.(fptoccc_funct)) F.(fptoccc_funct) ->
               FunctorObjEq Fbar' (rfccc_ext cD F);
}.

Arguments rfccc_ext  {C hC TC cTC J} _ {D cD}.
Arguments rfccc_cmp  {C hC TC cTC J} _ {D cD}.
Arguments rfccc_uniq {C hC TC cTC J} _ {D cD}.

(** ** Generated Ax-theory and the functor I

    Given an algebraic theory Th, its generated Ax-theory Th' has:
    - same signature as Th
    - no axioms (beta/eta are structural in AxThEq)
    The classifying category Cl(Th') = AxCl Th.(th_sig) is a CCC.

    The functor I : Cl(Th) → AxCl(Th) sends:
    - a list [α₁,...,αₙ] in Cl(Th) to the iterated product α₁ ×ax ... ×ax αₙ
    - a morphism (tuple of terms) to a corresponding single Ax-term via pairing *)

Definition alg_to_ax_type {Sg : Signature} (τ : Sg.(sg_ty)) : AxType Sg :=
  ax_ground τ.

(** Iterated product of a list of Ax-types (left-nested) *)
Fixpoint ax_iter_prod {Sg : Signature} (αs : list (AxType Sg)) : AxType Sg :=
  match αs with
  | []      => ax_unit
  | [α]     => α
  | α :: αs => ax_prod α (ax_iter_prod αs)
  end.

(** I on objects: map a list of ground sorts to their iterated product *)
Definition I_obj {Sg : Signature} (αs : list Sg.(sg_ty)) : AxType Sg :=
  ax_iter_prod (List.map ax_ground αs).

