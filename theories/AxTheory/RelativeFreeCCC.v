(** A custom induction principle for [Term Sg] that handles the
    [t_app] case via [Forall (P ∘ argument)]. *)
Lemma Term_ind_full (Sg : Signature) (P : Term Sg -> Prop) :
    (forall n, P (t_var n)) ->
    (forall (f : Sg.(sg_fun)) (args : list (Term Sg)),
        Forall P args -> P (t_app f args)) ->
    forall t, P t.
Proof.
  intros Hvar Happ.
  fix IH 1.
  intro t. destruct t as [n | f args].
  - apply Hvar.
  - apply Happ.
    induction args as [|a args' IHargs].
    + constructor.
