(** * AxTheory/RelativeFreeCCC.v
    Relatively free cartesian closed category generated by a
    finite-product category.

    Given a category C with finite products, a relatively free CCC
    generated by C is a CCC TC together with a finite-product-preserving
    functor J : C → TC such that:

    1. (Universal extension) For every CCC D and every
       finite-product-preserving functor F : C → D,
       there exists a CCC functor F̄ : TC → D with
       F̄ ∘ J naturally isomorphic to F.

    2. (Uniqueness) The extension F̄ is unique up to unique
       natural isomorphism compatible with the comparison F̄ ∘ J ≅ F.

    Proposition 4.10.2 states that the functor
       I : Cl(Th) → Cl(Th')
    (from the algebraic classifying category into the Ax classifying category)
    presents Cl(Th') as the relatively free CCC generated by Cl(Th). *)

