    Until those land, the umbrella statement
    [classifying_CCC_is_relatively_free] remains an Axiom: a
    paper-attributable universal property of the relative free CCC
    construction (cf. Lambek–Scott Part I §11; Pitts "Categorical Logic"
    §6.1).  Per project policy, paper-attributable axioms in
    foundational categorical-logic constructions are acceptable when
    flagged with informal-definition comments. *)

Section UniversalExtension.
  Context {Sg : Signature}.
  Context {D : Category} (cD : IsCartesianClosed D).
  Context (F : FPtoCCCFunctor (cl_finite_products Sg) cD).

  (** Object-level extension: recursive on [AxType Sg]. *)
  Fixpoint Fbar_obj (α : AxType Sg) : D.(Ob) :=
    match α with
    | ax_ground τ  => F.(fptoccc_funct) ## [τ]
    | ax_unit      => projT1 (@fp_terminal D cD.(ccc_fp))
    | ax_prod α' β => Fbar_obj α' ×{@fp_binary D cD.(ccc_fp)} Fbar_obj β
    | ax_exp  α' β => Fbar_obj α' ⇒{cD} Fbar_obj β
    end.

  (** Structural cases — definitional equalities packaged as Lemmas
      for downstream use in well-definedness proofs. *)

  Lemma Fbar_obj_unit :
      Fbar_obj ax_unit = projT1 (@fp_terminal D cD.(ccc_fp)).
  Proof. reflexivity. Qed.

  Lemma Fbar_obj_prod (α β : AxType Sg) :
      Fbar_obj (α ×ax β) =
      Fbar_obj α ×{@fp_binary D cD.(ccc_fp)} Fbar_obj β.
  Proof. reflexivity. Qed.

  Lemma Fbar_obj_exp (α β : AxType Sg) :
      Fbar_obj (α ⇒ax β) = Fbar_obj α ⇒{cD} Fbar_obj β.
  Proof. reflexivity. Qed.

  Lemma Fbar_obj_ground (τ : Sg.(sg_ty)) :
      Fbar_obj (ax_ground τ) = F.(fptoccc_funct) ## [τ].
  Proof. reflexivity. Qed.

End UniversalExtension.

Arguments Fbar_obj {Sg D cD} F α.

(** Object-level triangle — preliminary: on the special shape
    [I_obj [τ]] (singleton list), the witness reaches the F-image
    directly. *)

Lemma Fbar_obj_I_singleton {Sg : Signature}
    {D : Category} (cD : IsCartesianClosed D)
    (F : FPtoCCCFunctor (cl_finite_products Sg) cD)
    (τ : Sg.(sg_ty)) :
    Fbar_obj F (I_obj [τ]) = F.(fptoccc_funct) ## [τ].
Proof.
  unfold I_obj. simpl. reflexivity.
Qed.

(** Object-level triangle — empty list case: reaches D's terminal. *)

Lemma Fbar_obj_I_nil {Sg : Signature}
    {D : Category} (cD : IsCartesianClosed D)
    (F : FPtoCCCFunctor (cl_finite_products Sg) cD) :
    Fbar_obj F (I_obj []) = projT1 (@fp_terminal D cD.(ccc_fp)).
Proof.
  unfold I_obj. simpl. reflexivity.
Qed.

(** [classifying_CCC_is_relatively_free] — the universal property of
    the relative free CCC.  Paper-attributable (Lambek–Scott Part I §11
    "syntactic categories of typed lambda calculi"; Pitts "Categorical
    Logic" §6.1 Theorem 6.1.6).  The full discharge requires a
    morphism-level extension function [Fbar_term] together with its
    well-definedness modulo AxThEq, the CCC-functor laws on the
    extension, and the uniqueness clause — all standard but spanning
    several files of bookkeeping (cf. α R8's analogous L.3 completeness
    closure for the ATT side).  The OBJECT-LEVEL skeleton is delivered
    above as [Fbar_obj] + [Fbar_obj_unit] / [_prod] / [_exp] /
    [_ground] / [_I_singleton] / [_I_nil]; the morphism-level content
    is scoped for next rounds. *)

Axiom classifying_CCC_is_relatively_free : forall (Sg : Signature),
    RelativelyFreeCCC (cl_finite_products Sg) (axcl_ccc Sg) (I_fp_functor Sg).
